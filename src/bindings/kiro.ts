/**
 * Kiro CLI binding.
 *
 * Generates the `.kiro/` directory structure, `AGENTS.md`, and supporting
 * files from a parsed {@link TopologyAST}.
 *
 * Kiro uses JSON-based agent definitions (`.kiro/agents/<id>.json`),
 * markdown steering documents (`.kiro/steering/`), and MCP configuration
 * (`.kiro/settings/mcp.json`). Unlike Claude Code where agents are markdown
 * files with frontmatter, Kiro agents are plain JSON.
 *
 * @module
 */

import type {
  TopologyAST,
  AgentNode,
  GateNode,
  GroupNode,
  HumanNode,
  OrchestratorNode,
  HookDef,
  SkillDef,
  ToolBlockDef,
  SchemaFieldDef,
  SchemaType,
  RetryConfig,
  CircuitBreakerConfig,
  PromptVariant,
} from "../parser/ast.js";
import { deduplicateFiles } from "./types.js";
import type { BindingTarget, GeneratedFile } from "./types.js";

