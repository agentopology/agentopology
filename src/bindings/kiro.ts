/** Kiro CLI binding — generates .kiro/ directory structure and AGENTS.md. */
export const kiroBinding: BindingTarget = {
  name: "kiro",
  description:
    "AWS Kiro CLI — generates .kiro/ directory with JSON agents, steering docs, MCP config, and prompts.",

  scaffold(ast: TopologyAST): GeneratedFile[] {
    const files: GeneratedFile[] = [];

    // 1. Agent JSON files
    files.push(...generateAgents(ast));

    // 2. Steering documents
    files.push(generateProductSteering(ast));
    files.push(generateTechSteering(ast));
    files.push(generateStructureSteering(ast));

    // 2b. Per-agent behavioral steering docs (Wave 1-7)
    files.push(...generateAgentSteering(ast));

    // 2c. Human node steering docs (Wave 5)
    files.push(...generateHumanSteering(ast));

    // 2d. Group node steering docs (Wave 5)
    files.push(...generateGroupSteering(ast));

    // 3. MCP configuration
    const mcpFile = generateMcpJson(ast);
    if (mcpFile) files.push(mcpFile);

    // 4. Prompt files (triggers)
    files.push(...generatePromptFiles(ast));

    // 5. AGENTS.md (top-level flow narrative)
    files.push(generateAgentsMd(ast));

    // 6. Hook scripts
    files.push(...generateHookScripts(ast));

    // 7. Gate scripts
    files.push(...generateGateScripts(ast));

    // 8. Gate hook files
    files.push(...generateGateHooks(ast));

    // 9. Tool scripts
    files.push(...generateToolScripts(ast));

    // 10. Memory directories
    files.push(...generateMemory(ast));

    // 11. Metering
    const meteringFile = generateMetering(ast);
    if (meteringFile) files.push(meteringFile);

    return deduplicateFiles(files);
  },
};
