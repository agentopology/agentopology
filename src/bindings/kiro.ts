/** Generate memory structure directories under .kiro/. */
function generateMemory(ast: TopologyAST): GeneratedFile[] {
  const files: GeneratedFile[] = [];

  if (ast.memory.domains) {
    files.push(gitkeep(".kiro/domains"));
  }
  if (ast.memory.references) {
    files.push(gitkeep(".kiro/references"));
  }
  if (ast.memory.metrics) {
    files.push({
      path: ".kiro/metrics.jsonl",
      content: "",
    });
  }
  if (ast.memory.workspace) {
    files.push(gitkeep(".kiro/runs"));
  }

  return files;
}

