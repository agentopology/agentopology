/** Convert a kebab-case id to Title Case. */
function toTitle(id: string): string {
  return id
    .split(/[-_]/)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(" ");
}

/** Generate a shell script stub with a header comment. */
function shellStub(description: string): string {
  return [
    "#!/usr/bin/env bash",
    `# ${description}`,
    "# Auto-generated by agentopology scaffold — edit as needed.",
    "set -euo pipefail",
    "",
    'echo "TODO: implement this script"',
    "",
  ].join("\n");
}

/** Generate an empty directory marker (a .gitkeep file). */
function gitkeep(dirPath: string): GeneratedFile {
  return { path: `${dirPath}/.gitkeep`, content: "" };
}

/**
 * Map topology hook event names to Kiro event names.
 *
 * Kiro uses camelCase event names (preToolUse, postToolUse) instead of
 * PascalCase (PreToolUse, PostToolUse).
 */
function mapHookEvent(event: string): string {
  const eventMap: Record<string, string> = {
    PreToolUse: "preToolUse",
    PostToolUse: "postToolUse",
  };
  return eventMap[event] ?? event;
}

/**
 * Map topology tool names to Kiro tool names.
 *
 * Kiro uses lowercase tool names with fs_ prefixes for file operations.
 */
function mapToolName(tool: string): string {
  const toolMap: Record<string, string> = {
    Read: "read",
    Write: "write",
    Bash: "shell",
    Edit: "write",
  };
  return toolMap[tool] ?? tool.toLowerCase();
}

/**
 * Map topology tool names to Kiro hook matcher names.
 *
 * For hook matchers, Kiro uses the fs_read/fs_write/execute_bash convention.
 */
function mapMatcherName(matcher: string): string {
  const matcherMap: Record<string, string> = {
    Read: "fs_read",
    Write: "fs_write",
    Bash: "execute_bash",
    Edit: "fs_write",
  };
  return matcherMap[matcher] ?? matcher;
}

/** Format a SchemaType to a human-readable string. */
function formatSchemaType(t: SchemaType): string {
  switch (t.kind) {
    case "primitive":
      return t.value;
    case "array":
      return `${formatSchemaType(t.itemType)}[]`;
    case "enum":
      return t.values.join(" | ");
    case "ref":
      return t.name;
  }
}

/** Format schema fields as a markdown list. */
function formatSchemaFields(fields: SchemaFieldDef[]): string {
  return fields
    .map((f) => `- \`${f.name}${f.optional ? "?" : ""}\`: ${formatSchemaType(f.type)}`)
    .join("\n");
}

