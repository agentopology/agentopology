/**
 * Generate `.kiro/steering/product.md` — product context with topology
 * description, version, and patterns.
 */
function generateProductSteering(ast: TopologyAST): GeneratedFile {
  const sections: string[] = [];

  sections.push("---");
  sections.push("inclusion: always");
  sections.push("---");
  sections.push("");
  sections.push("# Product Context");
  sections.push("");

  if (ast.topology.description) {
    sections.push(ast.topology.description);
    sections.push("");
  }

  sections.push(`Version: ${ast.topology.version}`);
  if (ast.topology.patterns.length > 0) {
    sections.push(`Patterns: ${ast.topology.patterns.join(", ")}`);
  }
  sections.push("");

  // Schedules
  if (ast.schedules && ast.schedules.length > 0) {
    sections.push("## Schedules");
    sections.push("");
    for (const job of ast.schedules) {
      const expr = job.cron
        ? `cron: ${job.cron}`
        : job.every
          ? `every: ${job.every}`
          : "";
      const target = job.agent
        ? `agent: ${job.agent}`
        : job.action
          ? `action: ${job.action}`
          : "";
      sections.push(
        `- **${job.id}**: ${expr}${target ? ` -> ${target}` : ""}${job.enabled === false ? " (disabled)" : ""}`
      );
    }
    sections.push("");
  }

  // Interfaces
  if (ast.interfaces && ast.interfaces.length > 0) {
    sections.push("## Interfaces");
    sections.push("");
    for (const iface of ast.interfaces) {
      const typePart = iface.type ? ` (${iface.type})` : "";
      const configKeys = Object.keys(iface.config);
      const configPart =
        configKeys.length > 0
          ? ` — ${configKeys.map((k) => `${k}: ${iface.config[k]}`).join(", ")}`
          : "";
      sections.push(`- **${iface.id}**${typePart}${configPart}`);
    }
    sections.push("");
  }

  return {
    path: ".kiro/steering/product.md",
    content: sections.join("\n") + "\n",
  };
}

/**
 * Generate `.kiro/steering/tech.md` — technology context with models,
 * tool configuration, and provider settings.
 */
function generateTechSteering(ast: TopologyAST): GeneratedFile {
  const sections: string[] = [];

  sections.push("---");
  sections.push("inclusion: always");
  sections.push("---");
  sections.push("");
  sections.push("# Technology");
  sections.push("");

  // Models used
  const models = new Set<string>();
  for (const node of ast.nodes) {
    if (node.type === "orchestrator") {
      models.add((node as OrchestratorNode).model);
    } else if (node.type === "agent" && (node as AgentNode).model) {
      models.add((node as AgentNode).model!);
    }
  }
  if (models.size > 0) {
    sections.push("## Models");
    sections.push("");
    for (const model of models) {
      sections.push(`- ${model}`);
    }
    sections.push("");
  }

  // Providers
  if (ast.providers && ast.providers.length > 0) {
    sections.push("## Providers");
    sections.push("");
    for (const p of ast.providers) {
      const parts = [p.name];
      if (p.baseUrl) parts.push(`base: ${p.baseUrl}`);
      if (p.default) parts.push("(default)");
      sections.push(`- ${parts.join(" — ")}`);
    }
    sections.push("");
  }

  // Tools
  if (ast.toolDefs.length > 0) {
    sections.push("## Tools");
    sections.push("");
    for (const tool of ast.toolDefs) {
      sections.push(`- **${tool.id}**: ${tool.description}`);
    }
    sections.push("");
  }

  // Skills summary
  if (ast.skills.length > 0) {
    sections.push("## Skills");
    sections.push("");
    for (const skill of ast.skills) {
      const desc = skill.description ? ` — ${skill.description}` : "";
      sections.push(`- **${skill.id}**${desc}`);
    }
    sections.push("");
  }

  // Environment variables
  if (Object.keys(ast.env).length > 0) {
    sections.push("## Environment");
    sections.push("");
    for (const [key, val] of Object.entries(ast.env)) {
      const value = typeof val === "string" ? val : val.value;
      sections.push(`- \`${key}\`: ${value}`);
    }
    sections.push("");
  }

  return {
    path: ".kiro/steering/tech.md",
    content: sections.join("\n") + "\n",
  };
}

/**
 * Generate `.kiro/steering/structure.md` — file organization with
 * memory layout and read/write patterns.
 */
function generateStructureSteering(ast: TopologyAST): GeneratedFile {
  const sections: string[] = [];

  sections.push("---");
  sections.push("inclusion: always");
  sections.push("---");
  sections.push("");
  sections.push("# Structure");
  sections.push("");

  // Memory layout
  if (Object.keys(ast.memory).length > 0) {
    sections.push("## Memory Layout");
    sections.push("");
    if (ast.memory.domains) {
      sections.push("- **Domains:** .kiro/domains/");
    }
    if (ast.memory.references) {
      sections.push("- **References:** .kiro/references/");
    }
    if (ast.memory.metrics) {
      sections.push("- **Metrics:** .kiro/metrics.jsonl");
    }
    if (ast.memory.workspace) {
      sections.push("- **Workspace:** .kiro/runs/");
    }
    sections.push("");
  }

  // Read/Write patterns
  const agents = ast.nodes.filter((n) => n.type === "agent") as AgentNode[];
  const agentsWithAccess = agents.filter(
    (a) => (a.reads && a.reads.length > 0) || (a.writes && a.writes.length > 0)
  );
  if (agentsWithAccess.length > 0) {
    sections.push("## Read/Write Patterns");
    sections.push("");
    for (const agent of agentsWithAccess) {
      sections.push(`### ${toTitle(agent.id)}`);
      if (agent.reads && agent.reads.length > 0) {
        sections.push(`- Reads: ${agent.reads.join(", ")}`);
      }
      if (agent.writes && agent.writes.length > 0) {
        sections.push(`- Writes: ${agent.writes.join(", ")}`);
      }
      sections.push("");
    }
  }

  // Workspace protocol
  const workspace = ast.memory.workspace as Record<string, unknown> | undefined;
  if (workspace?.structure && Array.isArray(workspace.structure)) {
    sections.push("## Workspace Structure");
    sections.push("");
    for (const item of workspace.structure) {
      sections.push(`- ${item}`);
    }
    sections.push("");
  }

  return {
    path: ".kiro/steering/structure.md",
    content: sections.join("\n") + "\n",
  };
}

