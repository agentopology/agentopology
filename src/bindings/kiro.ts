/**
 * Generate `.kiro/agents/<id>.json` for each agent node.
 *
 * Each agent produces a single JSON file with name, description, prompt,
 * model, tools, hooks, resources, and MCP configuration.
 */
function generateAgents(ast: TopologyAST): GeneratedFile[] {
  const files: GeneratedFile[] = [];

  for (const node of ast.nodes) {
    if (node.type !== "agent") continue;
    const agent = node as AgentNode;

    const agentJson: Record<string, unknown> = {};
    agentJson.name = agent.id;

    // Description: prefer agent.description, then role description
    const desc =
      agent.description ??
      ast.roles[agent.role ?? ""] ??
      ast.roles[agent.id] ??
      agent.role;
    if (desc) {
      agentJson.description = desc;
    }

    // Prompt: build from role + inline prompt
    const promptParts: string[] = [];
    const roleText =
      ast.roles[agent.role ?? ""] ?? ast.roles[agent.id] ?? agent.role;
    if (roleText) {
      promptParts.push(`You are the ${toTitle(agent.id)} agent.\n\n## Role\n${roleText}`);
    } else {
      promptParts.push(`You are the ${toTitle(agent.id)} agent.`);
    }
    if (agent.prompt) {
      promptParts.push(`\n## Instructions\n\n${agent.prompt}`);
    }
    if (agent.reads && agent.reads.length > 0) {
      promptParts.push(`\n## Reads\n${agent.reads.map((r) => `- ${r}`).join("\n")}`);
    }
    if (agent.writes && agent.writes.length > 0) {
      promptParts.push(`\n## Writes\n${agent.writes.map((w) => `- ${w}`).join("\n")}`);
    }
    if (agent.outputs) {
      const outputLines = Object.entries(agent.outputs)
        .map(([field, values]) => `- ${field}: ${values.join(" | ")}`)
        .join("\n");
      promptParts.push(`\n## Outputs\n${outputLines}`);
    }
    if (agent.skills && agent.skills.length > 0) {
      promptParts.push(`\nSkills: ${agent.skills.join(", ")}`);
    }
    if (agent.behavior) {
      promptParts.push(`\nBehavior: ${agent.behavior}`);
    }
    if (agent.skip) {
      promptParts.push(`\nSkip when: ${agent.skip}`);
    }
    agentJson.prompt = promptParts.join("\n");

    // Model
    if (agent.model) {
      agentJson.model = agent.model;
    }

    // Timeout (Wave 1)
    if (agent.timeout) {
      agentJson.timeout = agent.timeout;
    }

    // Sampling params (Wave 1) — model config
    if (agent.temperature != null) {
      agentJson.temperature = agent.temperature;
    }
    if (agent.maxTokens != null) {
      agentJson.maxTokens = agent.maxTokens;
    }

    // Thinking/reasoning (Wave 1)
    if (agent.thinking) {
      agentJson.reasoning = agent.thinking;
    }

    // Max turns
    if (agent.maxTurns != null) {
      agentJson.maxTurns = agent.maxTurns;
    }

    // Tools — map topology tool names to Kiro names
    if (agent.tools && agent.tools.length > 0) {
      agentJson.tools = agent.tools.map(mapToolName);
    }

    // Allowed tools (subset that don't require confirmation)
    if (agent.extensions?.kiro?.allowedTools) {
      agentJson.allowedTools = agent.extensions.kiro.allowedTools;
    }

    // MCP servers as resources
    if (agent.mcpServers && agent.mcpServers.length > 0) {
      agentJson.resources = agent.mcpServers.map((s) => `mcp://${s}`);
    }

    // Reads as file:// resources
    if (agent.reads && agent.reads.length > 0) {
      const existing = (agentJson.resources as string[]) ?? [];
      agentJson.resources = [
        ...existing,
        ...agent.reads.map((r) => `file://${r}`),
      ];
    }

    // Per-agent hooks
    if (agent.hooks && agent.hooks.length > 0) {
      agentJson.hooks = agent.hooks.map((hook) => {
        const entry: Record<string, unknown> = {
          event: mapHookEvent(hook.on),
          command: hook.run,
        };
        if (hook.matcher) {
          entry.matcher = mapMatcherName(hook.matcher);
        }
        return entry;
      });
    }

    // Include MCP config if agent uses MCP servers
    if (agent.mcpServers && agent.mcpServers.length > 0) {
      agentJson.includeMcpJson = true;
    }

    // Merge kiro extension fields
    if (agent.extensions?.kiro) {
      const handled = new Set(["allowedTools"]);
      for (const [k, v] of Object.entries(agent.extensions.kiro)) {
        if (handled.has(k)) continue;
        agentJson[k] = v;
      }
    }

    files.push({
      path: `.kiro/agents/${agent.id}.json`,
      content: JSON.stringify(agentJson, null, 2) + "\n",
    });
  }

  return files;
}

