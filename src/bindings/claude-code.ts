/** Generate schedule scripts and crontab fragment. */
function generateScheduleScripts(ast: TopologyAST): GeneratedFile[] {
  const files: GeneratedFile[] = [];
  if (!ast.schedules || ast.schedules.length === 0) return files;

  const cronLines: string[] = [
    "# Crontab fragment — auto-generated by agentopology scaffold",
    "# Install with: crontab -l | cat - crontab.fragment | crontab -",
    "",
  ];

  for (const job of ast.schedules) {
    const target = job.agent ? `agent ${job.agent}` : job.action ? `action ${job.action}` : "unknown";
    const scriptPath = `scripts/schedule-${job.id}.sh`;

    // Generate the shell script
    const lines: string[] = [
      "#!/usr/bin/env bash",
      `# Schedule: ${job.id} — invokes ${target}`,
      "# Auto-generated by agentopology scaffold — edit as needed.",
      'set -euo pipefail',
      "",
    ];
    if (job.agent) {
      lines.push(`# Invoke agent: ${job.agent}`);
      lines.push(`claude --agent ${job.agent}`);
    } else if (job.action) {
      lines.push(`# Invoke action: ${job.action}`);
      lines.push(`bash scripts/action-${job.action}.sh`);
    }
    if (job.enabled === false) {
      lines.unshift("# DISABLED — this schedule is not enabled");
    }
    lines.push("");

    files.push({ path: scriptPath, content: lines.join("\n") });

    // Add crontab entry
    let cronExpr = job.cron ?? "# every: " + (job.every ?? "unknown");
    if (!job.cron && job.every) {
      // Convert common "every" expressions to cron
      const everyMap: Record<string, string> = {
        "1h": "0 * * * *",
        "1 hour": "0 * * * *",
        "hourly": "0 * * * *",
        "1d": "0 0 * * *",
        "1 day": "0 0 * * *",
        "daily": "0 0 * * *",
        "1w": "0 0 * * 0",
        "weekly": "0 0 * * 0",
      };
      cronExpr = everyMap[job.every.toLowerCase()] ?? `# every: ${job.every}`;
    }
    const enabledPrefix = job.enabled === false ? "# DISABLED: " : "";
    cronLines.push(`${enabledPrefix}${cronExpr} bash ${scriptPath}  # ${job.id}`);
  }

  cronLines.push("");
  files.push({ path: "crontab.fragment", content: cronLines.join("\n") });

  return files;
}

