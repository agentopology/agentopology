/** Generate tool script stubs. */
function generateToolScripts(ast: TopologyAST): GeneratedFile[] {
  const files: GeneratedFile[] = [];
  const name = ast.topology.name;

  for (const tool of ast.toolDefs) {
    const ext = tool.lang === "python" ? "py" : tool.lang === "node" ? "js" : "sh";
    const shebang =
      tool.lang === "python"
        ? "#!/usr/bin/env python3"
        : tool.lang === "node"
          ? "#!/usr/bin/env node"
          : "#!/usr/bin/env bash";

    const lines = [
      shebang,
      `# Tool: ${tool.id} — ${tool.description}`,
      "# Auto-generated by agentopology scaffold — edit as needed.",
      "",
    ];

    if (tool.lang === "bash" || !tool.lang) {
      lines.push('set -euo pipefail');
      lines.push("");
    }

    if (tool.args && tool.args.length > 0) {
      lines.push(`# Args: ${tool.args.join(", ")}`);
      lines.push("");
    }

    lines.push(`echo "TODO: implement ${tool.id}"`);
    lines.push("");

    files.push({
      path: `.claude/skills/${name}/scripts/${tool.id}.${ext}`,
      content: lines.join("\n"),
    });
  }

  return files;
}

/** Generate .claude/commands/X.md for each trigger. */
function generateCommandFiles(ast: TopologyAST): GeneratedFile[] {
  const files: GeneratedFile[] = [];

  for (const trigger of ast.triggers) {
    const sections: string[] = [];

    // Frontmatter
    const fm: Record<string, string | boolean | string[]> = {};
    if (ast.topology.description) {
      fm.description = `"${ast.topology.description}"`;
    }
    sections.push(frontmatter(fm));
    sections.push("");

    // Header
    sections.push(`# /${trigger.name}`);
    sections.push("");
    sections.push(trigger.pattern);
    sections.push("");

    // Arguments
    if (trigger.argument) {
      sections.push("## Arguments");
      sections.push(`- ${trigger.argument}: extracted from the command pattern`);
      sections.push("");
    }

    // Pipeline (from edges)
    if (ast.edges.length > 0) {
      sections.push("## Pipeline");
      for (const edge of ast.edges) {
        let line = `${edge.from} -> ${edge.to}`;
        if (edge.condition) line += ` [when ${edge.condition}]`;
        sections.push(`- ${line}`);
      }
      sections.push("");
    }

    // Agents table
    const agents = ast.nodes.filter((n) => n.type === "agent") as AgentNode[];
    if (agents.length > 0) {
      sections.push("## Agents");
      sections.push("| Agent | Phase | Model | Role |");
      sections.push("|-------|-------|-------|------|");
      for (const agent of agents) {
        const phase = agent.phase != null ? String(agent.phase) : "-";
        const model = agent.model ?? "-";
        const role = agent.role ?? "-";
        sections.push(`| ${agent.id} | ${phase} | ${model} | ${role} |`);
      }
      sections.push("");
    }

    files.push({
      path: `.claude/commands/${trigger.name}.md`,
      content: sections.join("\n") + "\n",
    });
  }

  return files;
}

/** Generate workspace-protocol.md if workspace memory has a protocol field. */
function generateWorkspaceProtocol(ast: TopologyAST): GeneratedFile | null {
  const name = ast.topology.name;
  const workspace = ast.memory.workspace as Record<string, unknown> | undefined;
  if (!workspace || !workspace.protocol) return null;

  const protocol = workspace.protocol as string;
  const sections: string[] = [];
  sections.push("# Workspace Protocol");
  sections.push("");

  // Directory structure from workspace.structure
  const structure = workspace.structure;
  if (structure && Array.isArray(structure)) {
    sections.push("## Directory Structure");
    for (const item of structure) {
      sections.push(`- ${item}`);
    }
    sections.push("");
  }

  // Read/Write rules from agent nodes
  const agents = ast.nodes.filter((n) => n.type === "agent") as AgentNode[];
  if (agents.length > 0) {
    sections.push("## Read/Write Rules");
    for (const agent of agents) {
      const reads = agent.reads ?? [];
      const writes = agent.writes ?? [];
      if (reads.length > 0 || writes.length > 0) {
        sections.push(`### ${toTitle(agent.id)}`);
        if (reads.length > 0) {
          sections.push(`- Reads: ${reads.join(", ")}`);
        }
        if (writes.length > 0) {
          sections.push(`- Writes: ${writes.join(", ")}`);
        }
        sections.push("");
      }
    }
  }

  return {
    path: `.claude/skills/${name}/${protocol}`,
    content: sections.join("\n") + "\n",
  };
}

/**
 * Generate context/instructions file (e.g. CLAUDE.md).
 *
 * If `ast.context.file` is set, generates that file. Otherwise generates
 * a default `CLAUDE.md` with basic topology info.
 */
function generateContextFile(ast: TopologyAST): GeneratedFile {
  const fileName = ast.context.file ?? "CLAUDE.md";
  const sections: string[] = [];

  sections.push(`# ${toTitle(ast.topology.name)}`);
  sections.push("");
  if (ast.topology.description) {
    sections.push(ast.topology.description);
    sections.push("");
  }
  sections.push(`Version: ${ast.topology.version}`);
  if (ast.topology.patterns.length > 0) {
    sections.push(`Patterns: ${ast.topology.patterns.join(", ")}`);
  }
  sections.push("");

  // Include references
  if (ast.context.includes && ast.context.includes.length > 0) {
    sections.push("## Includes");
    sections.push("");
    for (const inc of ast.context.includes) {
      sections.push(`- ${inc}`);
    }
    sections.push("");
  }

  return {
    path: fileName,
    content: sections.join("\n") + "\n",
  };
}

/**
 * Merge `ast.env` into the settings.json output under an `env` key.
 *
 * When `ast.providers` includes an "anthropic" provider with an api-key,
 * that env var is included automatically (enabling API-key mode instead
 * of subscription mode).
 *
 * Returns the env record if non-empty, otherwise null.
 */
function generateEnvToSettings(ast: TopologyAST): Record<string, string> | null {
  const env: Record<string, string> = { ...ast.env };

  // If an anthropic provider exists, scaffold the API key env var
  if (ast.providers && ast.providers.length > 0) {
    const anthropicProvider = ast.providers.find((p) => p.name === "anthropic");
    if (anthropicProvider?.apiKey) {
      // Extract env var name from ${ENV_VAR} format
      const envVarMatch = anthropicProvider.apiKey.match(/^\$\{(.+)\}$/);
      if (envVarMatch) {
        env[envVarMatch[1]] = anthropicProvider.apiKey;
      }
    }
  }

  if (Object.keys(env).length === 0) return null;
  return env;
}

/** Generate trigger documentation in the skill. */
function generateTriggers(ast: TopologyAST): string {
  if (ast.triggers.length === 0) return "";

  const lines = ["## Triggers", ""];
  for (const trigger of ast.triggers) {
    lines.push(`### /${trigger.name}`);
    lines.push(`Pattern: \`${trigger.pattern}\``);
    if (trigger.argument) lines.push(`Argument: ${trigger.argument}`);
    lines.push("");
  }
  return lines.join("\n");
}

