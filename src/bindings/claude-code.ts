/** Generate AGENT.md files for each agent node. */
function generateAgents(ast: TopologyAST): GeneratedFile[] {
  const files: GeneratedFile[] = [];

  for (const node of ast.nodes) {
    if (node.type !== "agent") continue;
    const agent = node as AgentNode;

    // Build frontmatter fields
    const fm: Record<string, string | boolean | string[]> = {};
    fm.name = agent.id;

    // Description: prefer agent.description, then role description from roles block
    const desc = agent.description ?? ast.roles[agent.role ?? ""] ?? ast.roles[agent.id] ?? agent.role;
    if (desc) {
      fm.description = `"${desc}"`;
    }

    if (agent.model) fm.model = agent.model;
    if (agent.maxTurns != null) fm.maxTurns = String(agent.maxTurns);
    if (agent.tools && agent.tools.length > 0) fm.tools = agent.tools;
    if (agent.mcpServers && agent.mcpServers.length > 0) fm.mcpServers = agent.mcpServers;
    if (agent.background === true) fm.background = true;

    if (agent.permissions) {
      const mapped = mapPermissionMode(agent.permissions);
      if (mapped) fm.permissionMode = mapped;
    }

    if (agent.isolation) fm.isolation = agent.isolation;

    // Merge claude-code extension fields into frontmatter
    if (agent.extensions?.["claude-code"]) {
      for (const [k, v] of Object.entries(agent.extensions["claude-code"])) {
        if (typeof v === "string") fm[k] = v;
        else if (typeof v === "boolean") fm[k] = v;
        else if (Array.isArray(v)) fm[k] = v as string[];
      }
    }

    // Build body
    const sections: string[] = [frontmatter(fm), ""];
    sections.push(`You are the ${toTitle(agent.id)} agent.`);
    sections.push("");

    // Role section
    const roleText = ast.roles[agent.role ?? ""] ?? ast.roles[agent.id] ?? agent.role;
    if (roleText) {
      sections.push("## Role");
      sections.push(roleText);
      sections.push("");
    }

    if (agent.prompt) {
      sections.push("## Instructions");
      sections.push("");
      sections.push(agent.prompt);
      sections.push("");
    }

    // Reads section
    if (agent.reads && agent.reads.length > 0) {
      sections.push("## Reads");
      for (const r of agent.reads) {
        sections.push(`- ${r}`);
      }
      sections.push("");
    }

    // Writes section
    if (agent.writes && agent.writes.length > 0) {
      sections.push("## Writes");
      for (const w of agent.writes) {
        sections.push(`- ${w}`);
      }
      sections.push("");
    }

    // Outputs section
    if (agent.outputs) {
      sections.push("## Outputs");
      for (const [field, values] of Object.entries(agent.outputs)) {
        sections.push(`- ${field}: ${values.join(" | ")}`);
      }
      sections.push("");
    }

    if (agent.skills && agent.skills.length > 0) {
      sections.push(`Skills: ${agent.skills.join(", ")}`);
    }
    if (agent.behavior) {
      sections.push(`Behavior: ${agent.behavior}`);
    }
    if (agent.skip) {
      sections.push(`Skip when: ${agent.skip}`);
    }

    files.push({
      path: `.claude/agents/${agent.id}/AGENT.md`,
      content: sections.join("\n") + "\n",
    });
  }

  return files;
}

/** Generate the main SKILL.md for the topology (orchestrator + flow + gates). */
function generateTopologySkill(ast: TopologyAST): GeneratedFile[] {
  const files: GeneratedFile[] = [];
  const name = ast.topology.name;

  // Build frontmatter
  const fm: Record<string, string | boolean | string[]> = {};
  fm.name = name;
  if (ast.topology.description) {
    fm.description = `"${ast.topology.description}"`;
  }
  fm.version = `"${ast.topology.version}"`;
  fm.topology = name;
  if (ast.topology.patterns.length > 0) {
    fm.patterns = ast.topology.patterns;
  }
  // Entry: first trigger's command file
  if (ast.triggers.length > 0) {
    fm.entry = `commands/${ast.triggers[0].name}.md`;
  }

  // Add skill-level fields from the first matching skill (if skills exist)
  // For the main topology skill, look for a skill with the same name as the topology
  const mainSkill = ast.skills.find((s) => s.id === name);
  if (mainSkill) {
    if (mainSkill.disableModelInvocation != null) {
      fm["disable-model-invocation"] = mainSkill.disableModelInvocation;
    }
    if (mainSkill.userInvocable != null) {
      fm["user-invocable"] = mainSkill.userInvocable;
    }
    if (mainSkill.context) fm.context = mainSkill.context;
    if (mainSkill.agent) fm.agent = mainSkill.agent;
    if (mainSkill.allowedTools && mainSkill.allowedTools.length > 0) {
      fm["allowed-tools"] = mainSkill.allowedTools;
    }
  }

  const sections: string[] = [];
  sections.push(frontmatter(fm));
  sections.push("");
  sections.push(`# ${toTitle(name)} Topology Skill`);
  sections.push("");
  if (ast.topology.description) {
    sections.push(ast.topology.description);
    sections.push("");
  }
  sections.push(`Version: ${ast.topology.version}`);
  if (ast.topology.patterns.length > 0) {
    sections.push(`Patterns: ${ast.topology.patterns.join(", ")}`);
  }
  sections.push("");

  // Orchestrator section
  const orch = ast.nodes.find((n) => n.type === "orchestrator");
  if (orch) {
    sections.push("## Orchestrator");
    sections.push("");
    sections.push(`Model: ${(orch as any).model}`);
    if ((orch as any).handles?.length > 0) {
      sections.push(`Handles: ${(orch as any).handles.join(", ")}`);
    }
    if ((orch as any).generates) {
      sections.push(`Generates: ${(orch as any).generates}`);
    }
    sections.push("");
  }

  // Flow section
  if (ast.edges.length > 0) {
    sections.push("## Flow");
    sections.push("");
    for (const edge of ast.edges) {
      let line = `${edge.from} -> ${edge.to}`;
      if (edge.condition) line += ` [when ${edge.condition}]`;
      if (edge.maxIterations) line += ` [max ${edge.maxIterations}]`;
      sections.push(`- ${line}`);
    }
    sections.push("");
  }

  // Gates section
  const gates = ast.nodes.filter((n) => n.type === "gate") as GateNode[];
  if (gates.length > 0) {
    sections.push("## Gates");
    sections.push("");
    for (const gate of gates) {
      sections.push(`### ${toTitle(gate.id)}`);
      if (gate.after) sections.push(`After: ${gate.after}`);
      if (gate.before) sections.push(`Before: ${gate.before}`);
      if (gate.run) sections.push(`Run: ${gate.run}`);
      if (gate.checks && gate.checks.length > 0) {
        sections.push(`Checks: ${gate.checks.join(", ")}`);
      }
      if (gate.onFail) sections.push(`On fail: ${gate.onFail}`);
      sections.push("");
    }
  }

  files.push({
    path: `.claude/skills/${name}/SKILL.md`,
    content: sections.join("\n") + "\n",
  });

  return files;
}

/** Generate skill-level files from the skills block. */
function generateSkills(ast: TopologyAST): GeneratedFile[] {
  const files: GeneratedFile[] = [];

  for (const skill of ast.skills) {
    const sections: string[] = [];

    // Build skill frontmatter
    const sfm: Record<string, string | boolean | string[]> = {};
    sfm.name = skill.id;
    if (skill.description) sfm.description = `"${skill.description}"`;
    if (skill.disableModelInvocation != null) {
      sfm["disable-model-invocation"] = skill.disableModelInvocation;
    }
    if (skill.userInvocable != null) {
      sfm["user-invocable"] = skill.userInvocable;
    }
    if (skill.context) sfm.context = skill.context;
    if (skill.agent) sfm.agent = skill.agent;
    if (skill.allowedTools && skill.allowedTools.length > 0) {
      sfm["allowed-tools"] = skill.allowedTools;
    }
    // Only emit frontmatter if there are fields beyond just the name
    if (Object.keys(sfm).length > 1) {
      sections.push(frontmatter(sfm));
      sections.push("");
    }

    sections.push(`# ${toTitle(skill.id)} Skill`);
    sections.push("");
    if (skill.description) {
      sections.push(skill.description);
      sections.push("");
    }
    if (skill.scripts && skill.scripts.length > 0) {
      sections.push("## Scripts");
      for (const s of skill.scripts) {
        sections.push(`- ${s}`);
      }
      sections.push("");
    }
    if (skill.domains && skill.domains.length > 0) {
      sections.push("## Domains");
      for (const d of skill.domains) {
        sections.push(`- ${d}`);
      }
      sections.push("");
    }
    if (skill.references && skill.references.length > 0) {
      sections.push("## References");
      for (const r of skill.references) {
        sections.push(`- ${r}`);
      }
      sections.push("");
    }

    files.push({
      path: `.claude/skills/${skill.id}/SKILL.md`,
      content: sections.join("\n") + "\n",
    });

    // Domain and reference directory markers
    if (skill.domains && skill.domains.length > 0) {
      files.push(gitkeep(`.claude/skills/${skill.id}/domains`));
    }
    if (skill.references && skill.references.length > 0) {
      files.push(gitkeep(`.claude/skills/${skill.id}/references`));
    }
  }

  return files;
}

/** Generate memory structure files (domains, references, metrics, workspace). */
function generateMemory(ast: TopologyAST): GeneratedFile[] {
  const files: GeneratedFile[] = [];
  const name = ast.topology.name;
  const memory = ast.memory;

  if (memory.domains) {
    files.push(gitkeep(`.claude/skills/${name}/domains`));
  }
  if (memory.references) {
    files.push(gitkeep(`.claude/skills/${name}/references`));
  }
  if (memory.metrics) {
    files.push({
      path: `.claude/skills/${name}/metrics.jsonl`,
      content: "",
    });
  }
  if (memory.workspace) {
    files.push(gitkeep(`.claude/skills/${name}/runs`));
  }

  return files;
}

/** Generate gate script stubs. */
function generateGateScripts(ast: TopologyAST): GeneratedFile[] {
  const files: GeneratedFile[] = [];
  const name = ast.topology.name;

  for (const node of ast.nodes) {
    if (node.type !== "gate") continue;
    const gate = node as GateNode;
    if (gate.run) {
      // Extract script name from the run command
      const scriptName = gate.run.replace(/^.*\//, "").replace(/\s.*$/, "");
      files.push({
        path: `.claude/skills/${name}/scripts/${scriptName}`,
        content: shellStub(`Gate: ${gate.id} — ${gate.run}`),
      });
    }
  }

  return files;
}

/** Generate hook script stubs for every hook's `run` field. */
function generateHookScripts(ast: TopologyAST): GeneratedFile[] {
  const files: GeneratedFile[] = [];
  const name = ast.topology.name;

  for (const hook of ast.hooks) {
    if (!hook.run) continue;
    // Extract script filename from the run command
    const scriptName = hook.run.replace(/^.*\//, "").replace(/\s.*$/, "");
    files.push({
      path: `.claude/skills/${name}/scripts/${scriptName}`,
      content: [
        "#!/usr/bin/env bash",
        `# Hook: ${hook.name} — fires on ${hook.on}`,
        "# Auto-generated by agentopology scaffold — edit as needed.",
        'set -euo pipefail',
        "",
        `echo "TODO: implement ${hook.name} hook"`,
        "",
      ].join("\n"),
    });
  }

  return files;
}

/**
 * Generate hook configuration in .claude/settings.json.
 *
 * Hooks are grouped by event name as an object:
 *   { "hooks": { "PostToolUse": [ { hooks: [...], matcher? } ], ... } }
 */
function generateSettings(ast: TopologyAST): GeneratedFile | null {
  const settings: Record<string, unknown> = {};
  const topologyName = ast.topology.name;

  // Hooks section — grouped by event name
  if (ast.hooks.length > 0) {
    const hooksByEvent: Record<string, unknown[]> = {};
    for (const hook of ast.hooks) {
      const eventName = hook.on;
      if (!hooksByEvent[eventName]) {
        hooksByEvent[eventName] = [];
      }

      // Build the script command with proper path prefix
      const scriptName = hook.run.replace(/^.*\//, "").replace(/\s.*$/, "");
      const command = `bash .claude/skills/${topologyName}/scripts/${scriptName}`;

      const hookEntry: Record<string, unknown> = {
        hooks: [
          {
            type: hook.type ?? "command",
            command,
            ...(hook.timeout ? { timeout: hook.timeout } : {}),
          },
        ],
      };

      // Only add matcher when it has a value
      if (hook.matcher) {
        hookEntry.matcher = hook.matcher;
      }

      hooksByEvent[eventName].push(hookEntry);
    }
    settings.hooks = hooksByEvent;
  }

  // Environment variables
  const envSettings = generateEnvToSettings(ast);
  if (envSettings) {
    settings.env = envSettings;
  }

  // Permissions section from settings block
  const perms = ast.settings;
  if (perms) {
    const allow = perms.allow as string[] | undefined;
    const deny = perms.deny as string[] | undefined;
    const ask = perms.ask as string[] | undefined;

    if (allow && allow.length > 0) settings.allow = allow;
    if (deny && deny.length > 0) settings.deny = deny;
    if (ask && ask.length > 0) settings.ask = ask;
  }

  if (Object.keys(settings).length === 0) return null;

  return {
    path: ".claude/settings.json",
    content: JSON.stringify(settings, null, 2) + "\n",
  };
}

/** Generate .mcp.json from mcp-servers block. Always includes env field. */
function generateMcpJson(ast: TopologyAST): GeneratedFile | null {
  if (Object.keys(ast.mcpServers).length === 0) return null;

  const mcpConfig: Record<string, unknown> = { mcpServers: {} };
  const servers = mcpConfig.mcpServers as Record<string, unknown>;

  for (const [name, config] of Object.entries(ast.mcpServers)) {
    const entry: Record<string, unknown> = {};
    let hasEnv = false;
    for (const [key, value] of Object.entries(config)) {
      if (key === "args" && Array.isArray(value)) {
        entry.args = value;
      } else {
        entry[key] = value;
      }
      if (key === "env") hasEnv = true;
    }
    // Always include env field
    if (!hasEnv) {
      entry.env = {};
    }
    servers[name] = entry;
  }

  return {
    path: ".mcp.json",
    content: JSON.stringify(mcpConfig, null, 2) + "\n",
  };
}

