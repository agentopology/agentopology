/** Generate the main SKILL.md for the topology (orchestrator + flow + gates). */
function generateTopologySkill(ast: TopologyAST): GeneratedFile[] {
  const files: GeneratedFile[] = [];
  const name = ast.topology.name;

  // Build frontmatter
  const fm: Record<string, string | boolean | string[]> = {};
  fm.name = name;
  if (ast.topology.description) {
    fm.description = `"${ast.topology.description}"`;
  }
  fm.version = `"${ast.topology.version}"`;
  fm.topology = name;
  if (ast.topology.patterns.length > 0) {
    fm.patterns = ast.topology.patterns;
  }
  // Entry: first trigger's command file
  if (ast.triggers.length > 0) {
    fm.entry = `commands/${ast.triggers[0].name}.md`;
  }

  // Add skill-level fields from the first matching skill (if skills exist)
  // For the main topology skill, look for a skill with the same name as the topology
  const mainSkill = ast.skills.find((s) => s.id === name);
  if (mainSkill) {
    if (mainSkill.disableModelInvocation != null) {
      fm["disable-model-invocation"] = mainSkill.disableModelInvocation;
    }
    if (mainSkill.userInvocable != null) {
      fm["user-invocable"] = mainSkill.userInvocable;
    }
    if (mainSkill.context) fm.context = mainSkill.context;
    if (mainSkill.agent) fm.agent = mainSkill.agent;
    if (mainSkill.allowedTools && mainSkill.allowedTools.length > 0) {
      fm["allowed-tools"] = mainSkill.allowedTools;
    }
  }

  const sections: string[] = [];
  sections.push(frontmatter(fm));
  sections.push("");
  sections.push(`# ${toTitle(name)} Topology Skill`);
  sections.push("");
  if (ast.topology.description) {
    sections.push(ast.topology.description);
    sections.push("");
  }
  sections.push(`Version: ${ast.topology.version}`);
  if (ast.topology.patterns.length > 0) {
    sections.push(`Patterns: ${ast.topology.patterns.join(", ")}`);
  }
  sections.push("");

  // Topology metadata
  if (ast.topology.foundations && ast.topology.foundations.length > 0) {
    sections.push(`Foundations: ${ast.topology.foundations.join(", ")}`);
  }
  if (ast.topology.advanced && ast.topology.advanced.length > 0) {
    sections.push(`Advanced: ${ast.topology.advanced.join(", ")}`);
  }
  if (ast.topology.domain) {
    sections.push(`Domain: ${ast.topology.domain}`);
  }
  if (ast.topology.timeout) {
    sections.push(`Timeout: ${ast.topology.timeout}`);
  }
  if (ast.topology.durable) {
    sections.push(`Durable: yes`);
  }
  if (ast.topology.foundations || ast.topology.advanced || ast.topology.domain || ast.topology.timeout || ast.topology.durable) {
    sections.push("");
  }

  // Fragment check — skip main skill generation for fragments
  if (ast.isFragment) {
    sections.push("*This is a fragment topology — it is meant to be included, not run standalone.*");
    sections.push("");
  }

  // Composition: params
  if (ast.params && ast.params.length > 0) {
    sections.push("## Parameters");
    sections.push("");
    for (const p of ast.params) {
      const req = p.required ? " (required)" : "";
      const def = p.default != null ? ` = ${p.default}` : "";
      sections.push(`- **${p.name}**: ${p.type}${req}${def}`);
    }
    sections.push("");
  }

  // Composition: interface endpoints
  if (ast.interfaceEndpoints) {
    sections.push("## Interface");
    sections.push("");
    sections.push(`- Entry: ${ast.interfaceEndpoints.entry}`);
    sections.push(`- Exit: ${ast.interfaceEndpoints.exit}`);
    sections.push("");
  }

  // Composition: imports
  if (ast.imports && ast.imports.length > 0) {
    sections.push("## Imports");
    sections.push("");
    for (const imp of ast.imports) {
      let line = `- **${imp.alias}** from \`${imp.source}\``;
      if (imp.sha256) line += ` (sha256: ${imp.sha256})`;
      if (imp.registry) line += ` [registry: ${imp.registryPackage}@${imp.registryVersion}]`;
      sections.push(line);
      if (Object.keys(imp.params).length > 0) {
        for (const [k, v] of Object.entries(imp.params)) {
          sections.push(`  - ${k}: ${v}`);
        }
      }
    }
    sections.push("");
  }

  // Composition: includes
  if (ast.includes && ast.includes.length > 0) {
    sections.push("## Includes");
    sections.push("");
    for (const inc of ast.includes) {
      sections.push(`- ${inc.source}`);
    }
    sections.push("");
  }

  // Error handler
  if (ast.topology.errorHandler) {
    sections.push("## Error Handling");
    sections.push("");
    sections.push(`Uncaught errors are handled by: **${ast.topology.errorHandler}**`);
    sections.push("");
  }

  // Orchestrator section
  const orch = ast.nodes.find((n) => n.type === "orchestrator");
  if (orch) {
    const orchNode = orch as OrchestratorNode;
    sections.push("## Orchestrator");
    sections.push("");
    sections.push(`Model: ${orchNode.model}`);
    if (orchNode.handles?.length > 0) {
      sections.push(`Handles: ${orchNode.handles.join(", ")}`);
    }
    if (orchNode.generates) {
      sections.push(`Generates: ${orchNode.generates}`);
    }
    if (orchNode.outputs) {
      sections.push("");
      sections.push("### Outputs");
      for (const [field, values] of Object.entries(orchNode.outputs)) {
        sections.push(`- ${field}: ${values.join(" | ")}`);
      }
    }
    sections.push("");
  }

  // Flow section (enhanced with error edges, join, weight, race, tolerance, reflection)
  if (ast.edges.length > 0) {
    sections.push("## Flow");
    sections.push("");
    for (const edge of ast.edges) {
      const arrow = edge.isError ? `-x${edge.errorType ? `[${edge.errorType}]` : ""}>` : "->";
      let line = `${edge.from} ${arrow} ${edge.to}`;
      if (edge.condition) line += ` [when ${edge.condition}]`;
      if (edge.maxIterations) line += ` [max ${edge.maxIterations}]`;
      if (edge.per) line += ` [per ${edge.per}]`;
      if (edge.weight != null) line += ` [weight ${edge.weight}]`;
      if (edge.race) line += ` [race]`;
      if (edge.tolerance != null) line += ` [tolerance: ${edge.tolerance}]`;
      if (edge.wait) line += ` [wait ${edge.wait}]`;
      if (edge.reflection) line += ` [reflection]`;
      sections.push(`- ${line}`);
    }
    sections.push("");
  }

  // Gates section
  const gates = ast.nodes.filter((n) => n.type === "gate") as GateNode[];
  if (gates.length > 0) {
    sections.push("## Gates");
    sections.push("");
    for (const gate of gates) {
      sections.push(`### ${toTitle(gate.id)}`);
      if (gate.after) sections.push(`After: ${gate.after}`);
      if (gate.before) sections.push(`Before: ${gate.before}`);
      if (gate.run) sections.push(`Run: ${gate.run}`);
      if (gate.checks && gate.checks.length > 0) {
        sections.push(`Checks: ${gate.checks.join(", ")}`);
      }
      if (gate.onFail) sections.push(`On fail: ${gate.onFail}`);
      sections.push("");
    }
  }

  // Schedules section
  if (ast.schedules.length > 0) {
    sections.push("## Schedules");
    sections.push("");
    for (const job of ast.schedules) {
      const expr = job.cron ? `cron: ${job.cron}` : job.every ? `every: ${job.every}` : "";
      const target = job.agent ? `agent: ${job.agent}` : job.action ? `action: ${job.action}` : "";
      sections.push(`- **${job.id}**: ${expr}${target ? ` -> ${target}` : ""}${job.enabled === false ? " (disabled)" : ""}`);
    }
    sections.push("");
  }

  // Interfaces section
  if (ast.interfaces.length > 0) {
    sections.push("## Interfaces");
    sections.push("");
    for (const iface of ast.interfaces) {
      const typePart = iface.type ? ` (${iface.type})` : "";
      const configKeys = Object.keys(iface.config);
      const configPart = configKeys.length > 0 ? ` — ${configKeys.map((k) => `${k}: ${iface.config[k]}`).join(", ")}` : "";
      sections.push(`- **${iface.id}**${typePart}${configPart}`);
    }
    sections.push("");
  }

  // Depth levels
  if (ast.depth && ast.depth.levels && ast.depth.levels.length > 0) {
    sections.push("## Depth Levels");
    sections.push("");
    if (ast.depth.factors.length > 0) {
      sections.push(`Factors: ${ast.depth.factors.join(", ")}`);
    }
    for (const level of ast.depth.levels) {
      let line = `- **Level ${level.level}**: ${level.label}`;
      if (level.omit.length > 0) line += ` (omit: ${level.omit.join(", ")})`;
      sections.push(line);
    }
    sections.push("");
  }

  // Batch settings
  if (ast.batch && Object.keys(ast.batch).length > 0) {
    sections.push("## Batch");
    sections.push("");
    for (const [key, value] of Object.entries(ast.batch)) {
      sections.push(`- ${key}: ${JSON.stringify(value)}`);
    }
    sections.push("");
  }

  // Environments
  if (ast.environments && Object.keys(ast.environments).length > 0) {
    sections.push("## Environments");
    sections.push("");
    for (const [envName, envConfig] of Object.entries(ast.environments)) {
      sections.push(`### ${envName}`);
      for (const [key, value] of Object.entries(envConfig)) {
        sections.push(`- ${key}: ${JSON.stringify(value)}`);
      }
      sections.push("");
    }
  }

  // Fallback chain from settings
  const fallbackChain = ast.settings?.["fallback-chain"];
  if (fallbackChain) {
    sections.push("## Fallback Chain");
    sections.push("");
    if (Array.isArray(fallbackChain)) {
      sections.push(`Models: ${(fallbackChain as string[]).join(" -> ")}`);
    } else {
      sections.push(`Fallback chain: ${fallbackChain}`);
    }
    sections.push("");
  }

  files.push({
    path: `.claude/skills/${name}/SKILL.md`,
    content: sections.join("\n") + "\n",
  });

  return files;
}

