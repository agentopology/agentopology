/**
 * Convert .at MCP tool notation (mcp.server.method) to Claude Code format (mcp__server__method).
 * Non-MCP tool names pass through unchanged.
 */
function convertMcpToolName(tool: string): string {
  if (!tool.startsWith("mcp.")) return tool;
  // mcp.server-name.method_name → mcp__server-name__method_name
  // Only replace the first two dots (mcp + server separator)
  const parts = tool.split(".");
  if (parts.length >= 3) {
    return `mcp__${parts[1]}__${parts.slice(2).join(".")}`;
  }
  return tool;
}

/**
 * Downshift all markdown headings by one level so they nest properly
 * inside ## Instructions. ## → ###, ### → ####, etc.
 */
function downshiftHeadings(text: string): string {
  return text.replace(/^(#{2,6})\s/gm, (_, hashes) => `#${hashes} `);
}

/** Convert a kebab-case id to Title Case. */
function toTitle(id: string): string {
  return id
    .split(/[-_]/)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(" ");
}

/**
 * Wrap content in a YAML frontmatter block.
 *
 * Supports scalar values, booleans, and arrays.
 * Arrays are rendered as multi-line YAML lists:
 *   key:
 *     - item1
 *     - item2
 */
function frontmatter(fields: Record<string, string | boolean | string[]>): string {
  const lines = ["---"];
  for (const [key, value] of Object.entries(fields)) {
    if (value === undefined || value === null) continue;
    if (Array.isArray(value)) {
      if (value.length === 0) continue;
      lines.push(`${key}:`);
      for (const item of value) {
        lines.push(`  - ${item}`);
      }
    } else if (typeof value === "boolean") {
      lines.push(`${key}: ${value}`);
    } else {
      lines.push(`${key}: ${value}`);
    }
  }
  lines.push("---");
  return lines.join("\n");
}

/** Generate a shell script stub with a header comment. */
function shellStub(description: string): string {
  return [
    "#!/usr/bin/env bash",
    `# ${description}`,
    "# Auto-generated by agentopology scaffold — edit as needed.",
    'set -euo pipefail',
    "",
    'echo "TODO: implement this script"',
    "",
  ].join("\n");
}

/** Generate an empty directory marker (a .gitkeep file). */
function gitkeep(dirPath: string): GeneratedFile {
  return { path: `${dirPath}/.gitkeep`, content: "", category: "machine" };
}

/**
 * Map topology permission strings to Claude Code permissionMode values.
 * Returns undefined when the permission mode should be omitted (default/autonomous).
 */
function mapPermissionMode(perm: string): string | undefined {
  // Claude Code native values — pass through
  const nativeValues = ["plan", "auto", "confirm", "bypassPermissions"];
  if (nativeValues.includes(perm)) {
    // "auto" is the default, omit it
    if (perm === "auto") return undefined;
    return perm;
  }

  // Topology-level semantic values
  switch (perm) {
    case "supervised":
      return "plan";
    case "autonomous":
      return undefined; // default, omit
    case "interactive":
      return "default";
    case "unrestricted":
      return "bypassPermissions";
    default:
      return perm; // unknown — pass through as-is
  }
}

/** Format a SchemaType to a human-readable string. Optionally resolves refs. */
function formatSchemaType(t: SchemaType, schemas?: SchemaDef[]): string {
  switch (t.kind) {
    case "primitive":
      return t.value;
    case "array":
      return `${formatSchemaType(t.itemType, schemas)}[]`;
    case "enum":
      return t.values.join(" | ");
    case "ref": {
      if (schemas) {
        const resolved = schemas.find((s) => s.id === t.name);
        if (resolved) {
          const fields = resolved.fields.map((f) => `${f.name}${f.optional ? "?" : ""}: ${formatSchemaType(f.type, schemas)}`);
          return `${t.name} { ${fields.join(", ")} }`;
        }
      }
      return t.name;
    }
  }
}

/** Format a SchemaFieldDef to a readable line. */
function formatSchemaField(f: SchemaFieldDef): string {
  const opt = f.optional ? "?" : "";
  return `- ${f.name}${opt}: ${formatSchemaType(f.type)}`;
}

