/**
 * Generate hook configuration in .claude/settings.json.
 *
 * Hooks are grouped by event name as an object:
 *   { "hooks": { "PostToolUse": [ { hooks: [...], matcher? } ], ... } }
 */
function generateSettings(ast: TopologyAST): GeneratedFile | null {
  const settings: Record<string, unknown> = {};
  const topologyName = ast.topology.name;

  // Hooks section — grouped by event name
  const hooksByEvent: Record<string, unknown[]> = {};

  for (const hook of ast.hooks) {
    const eventName = hook.on;
    if (!hooksByEvent[eventName]) {
      hooksByEvent[eventName] = [];
    }

    // Build the script command with proper path prefix
    const scriptName = hook.run.replace(/^.*\//, "").replace(/\s.*$/, "");
    const command = `bash .claude/skills/${topologyName}/scripts/${scriptName}`;

    const hookEntry: Record<string, unknown> = {
      hooks: [
        {
          type: hook.type ?? "command",
          command,
          ...(hook.timeout ? { timeout: hook.timeout } : {}),
        },
      ],
    };

    // Only add matcher when it has a value
    if (hook.matcher) {
      hookEntry.matcher = hook.matcher;
    }

    hooksByEvent[eventName].push(hookEntry);
  }

  // Per-agent hooks are now rendered in AGENT.md frontmatter (not in settings.json).
  // This is the Claude Code native approach: hooks in the agent file are scoped
  // to that agent automatically.

  // Gate enforcement — compile gates with run: to SubagentStop hooks
  // Gates fire AFTER an agent finishes (SubagentStop), not before a tool runs.
  // The matcher is the gate's `after:` agent name so the hook fires when that
  // specific agent completes.
  const gates = ast.nodes.filter((n) => n.type === "gate") as GateNode[];
  for (const gate of gates) {
    if (!gate.run) continue;
    if (gate.behavior === "advisory") continue; // advisory gates stay in markdown only

    const eventName = "SubagentStop";
    if (!hooksByEvent[eventName]) hooksByEvent[eventName] = [];

    const scriptName = `gate-${gate.id}.sh`;
    const command = `bash .claude/skills/${topologyName}/scripts/${scriptName}`;

    const hookEntry: Record<string, unknown> = {
      hooks: [
        {
          type: "command",
          command,
        },
      ],
    };

    // Use the gate's after: agent as matcher so the hook only fires
    // when that specific agent completes. Without after:, match all agents.
    if (gate.after) {
      hookEntry.matcher = gate.after;
    }

    hooksByEvent[eventName].push(hookEntry);
  }

  // Write hooksByEvent to settings if any hooks or gates were registered
  if (Object.keys(hooksByEvent).length > 0) {
    settings.hooks = hooksByEvent;
  }

  // Environment variables
  const envSettings = generateEnvToSettings(ast);
  if (envSettings) {
    settings.env = envSettings;
  }

  // Permissions section from settings block
  const perms = ast.settings;
  if (perms) {
    const allow = perms.allow as string[] | undefined;
    const deny = perms.deny as string[] | undefined;
    const ask = perms.ask as string[] | undefined;

    if (allow && allow.length > 0) settings.allow = allow;
    if (deny && deny.length > 0) settings.deny = deny;
    if (ask && ask.length > 0) settings.ask = ask;
  }

  // Defaults documentation
  if (ast.defaults) {
    const d = ast.defaults;
    const defaultsDoc: Record<string, unknown> = {};
    if (d.temperature != null) defaultsDoc.temperature = d.temperature;
    if (d.maxTokens != null) defaultsDoc.maxTokens = d.maxTokens;
    if (d.topP != null) defaultsDoc.topP = d.topP;
    if (d.topK != null) defaultsDoc.topK = d.topK;
    if (d.stop) defaultsDoc.stop = d.stop;
    if (d.seed != null) defaultsDoc.seed = d.seed;
    if (d.thinking) defaultsDoc.thinking = d.thinking;
    if (d.thinkingBudget != null) defaultsDoc.thinkingBudget = d.thinkingBudget;
    if (d.outputFormat) defaultsDoc.outputFormat = d.outputFormat;
    if (d.timeout) defaultsDoc.timeout = d.timeout;
    if (d.logLevel) defaultsDoc.logLevel = d.logLevel;
    if (Object.keys(defaultsDoc).length > 0) {
      settings._defaults = defaultsDoc;
    }
  }

  // Observability documentation
  if (ast.observability) {
    const o = ast.observability;
    const obsDoc: Record<string, unknown> = {
      enabled: o.enabled,
      level: o.level,
      exporter: o.exporter,
      sampleRate: o.sampleRate,
    };
    if (o.endpoint) obsDoc.endpoint = o.endpoint;
    if (o.service) obsDoc.service = o.service;
    obsDoc.capture = o.capture;
    obsDoc.spans = o.spans;
    settings._observability = obsDoc;
  }

  // Top-level extensions for claude-code
  if (ast.extensions?.["claude-code"]) {
    for (const [k, v] of Object.entries(ast.extensions["claude-code"])) {
      settings[k] = v;
    }
  }

  if (Object.keys(settings).length === 0) return null;

  return {
    path: ".claude/settings.json",
    content: JSON.stringify(settings, null, 2) + "\n",
    category: "shared-config",
  };
}

/**
 * Backend → MCP server mapping.
 *
 * Only packages verified to exist on npm or PyPI are included.
 * npm packages use `npx -y`, Python packages use `uvx`.
 * `pathFlag` controls how the DB path is passed (positional, --db, --db-path, etc.).
 *
 * Verified 2026-03-21:
 *   npm:  kuzu-mcp-server@0.1.0, mongodb-mcp-server@1.8.1
 *   pip:  mcp-server-sqlite@2025.4.25, mcp-server-qdrant@0.8.1,
 *         mcp-server-neo4j@0.1.8, chroma-mcp@0.2.6
 */
const STORE_MCP_COMMANDS: Record<string, { command: string; args: string[]; pathFlag?: string }> = {
  // npm packages
  "kuzu": { command: "npx", args: ["-y", "kuzu-mcp-server"] },                  // positional
  "mongodb": { command: "npx", args: ["-y", "mongodb-mcp-server"], pathFlag: "--uri" },
  // Python MCP servers (via uvx)
  "sqlite": { command: "uvx", args: ["mcp-server-sqlite"], pathFlag: "--db-path" },
  "sqlite-vec": { command: "uvx", args: ["mcp-server-sqlite"], pathFlag: "--db-path" },
  "qdrant": { command: "uvx", args: ["mcp-server-qdrant"], pathFlag: "--qdrant-url" },
  "neo4j": { command: "uvx", args: ["mcp-server-neo4j"], pathFlag: "--db-uri" },
  "chroma": { command: "uvx", args: ["chroma-mcp"], pathFlag: "--host" },
  // No verified MCP servers: lancedb, pinecone, pgvector, falkordb
  // These backends use skill-based access (slash commands) instead
};

function generateMcpJson(ast: TopologyAST): GeneratedFile | null {
  const stores = ast.stores ?? [];
  if (Object.keys(ast.mcpServers).length === 0 && stores.length === 0) return null;

  const mcpConfig: Record<string, unknown> = { mcpServers: {} };
  const servers = mcpConfig.mcpServers as Record<string, unknown>;

  for (const [name, config] of Object.entries(ast.mcpServers)) {
    const entry: Record<string, unknown> = {};
    for (const [key, value] of Object.entries(config)) {
      if (key === "command" || key === "url") {
        entry[key] = value;
      } else if (key === "args" && Array.isArray(value)) {
        entry.args = value;
      } else if (key === "env" && typeof value === "object" && value !== null) {
        entry.env = value;
      }
      // Skip unknown fields — parser may leak env vars to top level
    }
    // Only include servers that have a command or url (filter parser artifacts)
    if (!("command" in entry) && !("url" in entry)) continue;
    // Always include env field for Claude Code .mcp.json
    if (!("env" in entry)) {
      entry.env = {};
    }
    servers[name] = entry;
  }

  // Merge memory store MCP server entries
  for (const store of stores) {
    const mcpCmd = STORE_MCP_COMMANDS[store.backend];
    if (!mcpCmd) {
      // Backend not in the known MCP map — skip (skill-based access only)
      continue;
    }

    const args = [...mcpCmd.args];
    const storePath = store.path ?? `.claude/stores/${store.id}`;

    // Each server has its own CLI convention for the DB path
    if (mcpCmd.pathFlag) {
      args.push(mcpCmd.pathFlag, storePath);
    } else {
      // No pathFlag = positional argument (e.g., kuzu-mcp-server ./path)
      args.push(storePath);
    }

    const env: Record<string, string> = {};
    if (store.connection) {
      env.CONNECTION_STRING = store.connection;
    }

    servers[store.id] = {
      command: mcpCmd.command,
      args,
      env,
    };
  }

  // If no servers were added (all stores had unsupported backends), return null
  if (Object.keys(servers).length === 0) return null;

  return {
    path: ".mcp.json",
    content: JSON.stringify(mcpConfig, null, 2) + "\n",
    category: "shared-config",
  };
}

