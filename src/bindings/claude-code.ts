/** Generate script stubs for each ActionNode. */
function generateActionNodes(ast: TopologyAST): GeneratedFile[] {
  const files: GeneratedFile[] = [];
  const name = ast.topology.name;

  for (const node of ast.nodes) {
    if (node.type !== "action") continue;
    const action = node as ActionNode;

    const lines: string[] = ["#!/usr/bin/env bash"];
    lines.push(`# Action: ${action.id} — ${action.description ?? action.label}`);
    lines.push("# Auto-generated by agentopology scaffold — edit as needed.");
    lines.push('set -euo pipefail');
    lines.push("");

    if (action.timeout) {
      lines.push(`# Timeout: ${action.timeout}`);
    }
    if (action.onFail) {
      lines.push(`# On failure: ${action.onFail}`);
    }
    if (action.join) {
      lines.push(`# Join semantics: ${action.join}`);
    }

    switch (action.kind) {
      case "external":
        lines.push(`# External source: ${action.source ?? "unknown"}`);
        lines.push("");
        lines.push(`echo "TODO: implement external action — fetch from ${action.source ?? "source"}"`);
        break;
      case "inline":
        if (action.commands && action.commands.length > 0) {
          lines.push("");
          for (const cmd of action.commands) {
            lines.push(cmd);
          }
        } else {
          lines.push("");
          lines.push('echo "TODO: implement inline action"');
        }
        break;
      case "git":
        lines.push("");
        lines.push("# Git operation");
        if (action.commands && action.commands.length > 0) {
          for (const cmd of action.commands) {
            lines.push(cmd);
          }
        } else {
          lines.push('echo "TODO: implement git action"');
        }
        break;
      case "report":
        lines.push("");
        lines.push("# Report generation");
        lines.push('echo "TODO: implement report action"');
        break;
      default:
        lines.push("");
        lines.push(`echo "TODO: implement ${action.kind ?? "unknown"} action"`);
        break;
    }

    lines.push("");

    files.push({
      path: `.claude/skills/${name}/scripts/action-${action.id}.sh`,
      content: lines.join("\n"),
    });
  }

  return files;
}

/** Generate AGENT.md files for each agent node. */
function generateAgents(ast: TopologyAST): GeneratedFile[] {
  const files: GeneratedFile[] = [];

  for (const node of ast.nodes) {
    if (node.type !== "agent") continue;
    const agent = node as AgentNode;

    // Build frontmatter fields
    const fm: Record<string, string | boolean | string[]> = {};
    fm.name = agent.id;

    // Description: prefer agent.description, then role description from roles block
    const desc = agent.description ?? ast.roles[agent.role ?? ""] ?? ast.roles[agent.id] ?? agent.role;
    if (desc) {
      fm.description = `"${desc}"`;
    }

    if (agent.model) fm.model = agent.model;
    if (agent.maxTurns != null) fm.maxTurns = String(agent.maxTurns);
    if (agent.tools && agent.tools.length > 0) fm.tools = agent.tools;
    if (agent.disallowedTools && agent.disallowedTools.length > 0) fm["disallowed-tools"] = agent.disallowedTools;
    if (agent.mcpServers && agent.mcpServers.length > 0) fm.mcpServers = agent.mcpServers;
    if (agent.background === true) fm.background = true;

    if (agent.permissions) {
      const mapped = mapPermissionMode(agent.permissions);
      if (mapped) fm.permissionMode = mapped;
    }

    if (agent.isolation) fm.isolation = agent.isolation;

    if (agent.sandbox != null) {
      fm.sandbox = typeof agent.sandbox === "boolean" ? agent.sandbox : agent.sandbox;
    }

    if (agent.fallbackChain && agent.fallbackChain.length > 0) {
      fm["fallback-chain"] = agent.fallbackChain.join(", ");
    }

    // Merge claude-code extension fields into frontmatter
    if (agent.extensions?.["claude-code"]) {
      for (const [k, v] of Object.entries(agent.extensions["claude-code"])) {
        if (typeof v === "string") fm[k] = v;
        else if (typeof v === "boolean") fm[k] = v;
        else if (Array.isArray(v)) fm[k] = v as string[];
      }
    }

    // Build body
    const sections: string[] = [frontmatter(fm), ""];
    const displayName = agent.label || toTitle(agent.id);
    sections.push(`You are the ${displayName} agent.`);
    sections.push("");

    // Role section
    const roleText = ast.roles[agent.role ?? ""] ?? ast.roles[agent.id] ?? agent.role;
    if (roleText) {
      sections.push("## Role");
      sections.push(roleText);
      sections.push("");
    }

    if (agent.prompt) {
      sections.push("## Instructions");
      sections.push("");
      sections.push(agent.prompt);
      sections.push("");
    }

    // Reads section
    if (agent.reads && agent.reads.length > 0) {
      sections.push("## Reads");
      for (const r of agent.reads) {
        sections.push(`- ${r}`);
      }
      sections.push("");
    }

    // Writes section
    if (agent.writes && agent.writes.length > 0) {
      sections.push("## Writes");
      for (const w of agent.writes) {
        sections.push(`- ${w}`);
      }
      sections.push("");
    }

    // Outputs section
    if (agent.outputs) {
      sections.push("## Outputs");
      for (const [field, values] of Object.entries(agent.outputs)) {
        sections.push(`- ${field}: ${values.join(" | ")}`);
      }
      sections.push("");
    }

    // Timeout instruction
    if (agent.timeout) {
      sections.push(`You have a maximum of ${agent.timeout} to complete your work.`);
      sections.push("");
    }

    // On-fail instruction
    if (agent.onFail) {
      sections.push(`If you fail: ${agent.onFail}`);
      sections.push("");
    }

    // Retry configuration
    if (agent.retry != null) {
      if (typeof agent.retry === "number") {
        sections.push(`Retry up to ${agent.retry} times on failure.`);
      } else {
        const r = agent.retry as RetryConfig;
        const parts = [`max ${r.max} attempts`];
        if (r.backoff) parts.push(`${r.backoff} backoff`);
        if (r.interval) parts.push(`interval ${r.interval}`);
        if (r.maxInterval) parts.push(`max interval ${r.maxInterval}`);
        if (r.jitter) parts.push(`with jitter`);
        if (r.nonRetryable && r.nonRetryable.length > 0) {
          parts.push(`non-retryable: ${r.nonRetryable.join(", ")}`);
        }
        sections.push(`Retry strategy: ${parts.join(", ")}.`);
      }
      sections.push("");
    }

    // Model Configuration section
    const hasModelConfig =
      agent.temperature != null ||
      agent.maxTokens != null ||
      agent.topP != null ||
      agent.topK != null ||
      agent.stop != null ||
      agent.seed != null;
    if (hasModelConfig) {
      sections.push("## Model Configuration");
      if (agent.temperature != null) sections.push(`- Temperature: ${agent.temperature}`);
      if (agent.maxTokens != null) sections.push(`- Max tokens: ${agent.maxTokens}`);
      if (agent.topP != null) sections.push(`- Top-p: ${agent.topP}`);
      if (agent.topK != null) sections.push(`- Top-k: ${agent.topK}`);
      if (agent.stop) sections.push(`- Stop sequences: ${agent.stop.join(", ")}`);
      if (agent.seed != null) sections.push(`- Seed: ${agent.seed}`);
      sections.push("");
    }

    // Thinking
    if (agent.thinking) {
      let thinkingLine = `Use ${agent.thinking} level reasoning.`;
      if (agent.thinkingBudget != null) {
        thinkingLine = `Use ${agent.thinking} level reasoning (budget: ${agent.thinkingBudget} tokens).`;
      }
      sections.push(thinkingLine);
      sections.push("");
    }

    // Output format
    if (agent.outputFormat) {
      sections.push(`Output format: ${agent.outputFormat}`);
      sections.push("");
    }

    // Log level
    if (agent.logLevel) {
      sections.push(`Log level: ${agent.logLevel}`);
      sections.push("");
    }

    // Join semantics
    if (agent.join) {
      sections.push(`Wait strategy: ${agent.join} (wait for upstream agents)`);
      sections.push("");
    }

    // Circuit breaker
    if (agent.circuitBreaker) {
      const cb = agent.circuitBreaker;
      sections.push("## Circuit Breaker");
      sections.push(`- Failure threshold: ${cb.threshold}`);
      sections.push(`- Window: ${cb.window}`);
      sections.push(`- Cooldown: ${cb.cooldown}`);
      sections.push("");
    }

    // Compensates
    if (agent.compensates) {
      sections.push(`This agent compensates (can undo) the work of: ${agent.compensates}`);
      sections.push("");
    }

    // Input Schema
    if (agent.inputSchema && agent.inputSchema.length > 0) {
      sections.push("## Input Schema");
      for (const field of agent.inputSchema) {
        sections.push(formatSchemaField(field));
      }
      sections.push("");
    }

    // Output Schema
    if (agent.outputSchema && agent.outputSchema.length > 0) {
      sections.push("## Output Schema");
      for (const field of agent.outputSchema) {
        sections.push(formatSchemaField(field));
      }
      sections.push("");
    }

    // Rate limit
    if (agent.rateLimit) {
      sections.push(`Rate limit: ${agent.rateLimit}`);
      sections.push("");
    }

    // Artifacts: produces/consumes
    if ((agent.produces && agent.produces.length > 0) || (agent.consumes && agent.consumes.length > 0)) {
      sections.push("## Artifacts");
      if (agent.produces && agent.produces.length > 0) {
        sections.push(`Produces: ${agent.produces.join(", ")}`);
      }
      if (agent.consumes && agent.consumes.length > 0) {
        sections.push(`Consumes: ${agent.consumes.join(", ")}`);
      }
      sections.push("");
    }

    // Variants
    if (agent.variants && agent.variants.length > 0) {
      sections.push("## Variants");
      for (const v of agent.variants) {
        const parts = [`**${v.id}** (weight: ${v.weight})`];
        if (v.model) parts.push(`model: ${v.model}`);
        if (v.temperature != null) parts.push(`temperature: ${v.temperature}`);
        sections.push(`- ${parts.join(", ")}`);
        if (v.prompt) {
          sections.push(`  Prompt: ${v.prompt}`);
        }
      }
      sections.push("");
    }

    // Scale
    if (agent.scale) {
      const s = agent.scale;
      sections.push("## Scale");
      sections.push(`- Mode: ${s.mode}`);
      sections.push(`- By: ${s.by}`);
      sections.push(`- Min: ${s.min}`);
      sections.push(`- Max: ${s.max}`);
      if (s.batchSize != null) sections.push(`- Batch size: ${s.batchSize}`);
      sections.push("");
    }

    // Invocation
    if (agent.invocation) {
      sections.push(`Invocation: ${agent.invocation}`);
      sections.push("");
    }

    if (agent.skills && agent.skills.length > 0) {
      sections.push(`Skills: ${agent.skills.join(", ")}`);
    }
    if (agent.behavior) {
      sections.push(`Behavior: ${agent.behavior}`);
    }
    if (agent.skip) {
      sections.push(`Skip when: ${agent.skip}`);
    }

    files.push({
      path: `.claude/agents/${agent.id}/AGENT.md`,
      content: sections.join("\n") + "\n",
    });
  }

  return files;
}

