/** Generate schedule scripts and crontab fragment. */
function generateScheduleScripts(ast: TopologyAST): GeneratedFile[] {
  const files: GeneratedFile[] = [];
  if (!ast.schedules || ast.schedules.length === 0) return files;

  const cronLines: string[] = [
    "# Crontab fragment — auto-generated by agentopology scaffold",
    "# Install with: crontab -l | cat - crontab.fragment | crontab -",
    "",
  ];

  for (const job of ast.schedules) {
    const target = job.agent ? `agent ${job.agent}` : job.action ? `action ${job.action}` : "unknown";
    const scriptPath = `scripts/schedule-${job.id}.sh`;

    // Generate the shell script
    const lines: string[] = [
      "#!/usr/bin/env bash",
      `# Schedule: ${job.id} — invokes ${target}`,
      "# Auto-generated by agentopology scaffold — edit as needed.",
      'set -euo pipefail',
      "",
    ];
    if (job.agent) {
      lines.push(`# Invoke agent: ${job.agent}`);
      lines.push(`claude --agent ${job.agent}`);
    } else if (job.action) {
      lines.push(`# Invoke action: ${job.action}`);
      lines.push(`bash scripts/action-${job.action}.sh`);
    }
    if (job.enabled === false) {
      lines.unshift("# DISABLED — this schedule is not enabled");
    }
    lines.push("");

    files.push({ path: scriptPath, content: lines.join("\n"), category: "script" });

    // Add crontab entry
    let cronExpr = job.cron ?? "# every: " + (job.every ?? "unknown");
    if (!job.cron && job.every) {
      // Convert common "every" expressions to cron
      const everyMap: Record<string, string> = {
        "1h": "0 * * * *",
        "1 hour": "0 * * * *",
        "hourly": "0 * * * *",
        "1d": "0 0 * * *",
        "1 day": "0 0 * * *",
        "daily": "0 0 * * *",
        "1w": "0 0 * * 0",
        "weekly": "0 0 * * 0",
      };
      cronExpr = everyMap[job.every.toLowerCase()] ?? `# every: ${job.every}`;
    }
    const enabledPrefix = job.enabled === false ? "# DISABLED: " : "";
    cronLines.push(`${enabledPrefix}${cronExpr} bash ${scriptPath}  # ${job.id}`);
  }

  cronLines.push("");
  files.push({ path: "crontab.fragment", content: cronLines.join("\n"), category: "machine" });

  return files;
}

/** Generate .claude/commands/X.md for each trigger. */
function generateCommandFiles(ast: TopologyAST): GeneratedFile[] {
  const files: GeneratedFile[] = [];

  for (const trigger of ast.triggers) {
    const sections: string[] = [];

    // Frontmatter
    const fm: Record<string, string | boolean | string[]> = {};
    if (ast.topology.description) {
      fm.description = `"${ast.topology.description}"`;
    }
    sections.push(frontmatter(fm));
    sections.push("");

    // Header
    sections.push(`# /${trigger.name}`);
    sections.push("");
    sections.push(trigger.pattern);
    sections.push("");

    // Arguments
    if (trigger.argument) {
      sections.push("## Arguments");
      sections.push(`- ${trigger.argument}: extracted from the command pattern`);
      sections.push("");
    }

    // Pipeline (from edges, enhanced)
    if (ast.edges.length > 0) {
      sections.push("## Pipeline");
      for (const edge of ast.edges) {
        const arrow = edge.isError ? `-x${edge.errorType ? `[${edge.errorType}]` : ""}>` : "->";
        let line = `${edge.from} ${arrow} ${edge.to}`;
        if (edge.condition) {
          const cleanCond = edge.condition
            .replace(/\]\s*\[max\s+\d+\s*$/i, "")
            .replace(/\]\s*$/, "")
            .trim();
          line += ` [when ${cleanCond}]`;
        }
        if (edge.maxIterations) line += ` [max ${edge.maxIterations}]`;
        if (edge.weight != null) line += ` [weight ${edge.weight}]`;
        if (edge.race) line += ` [race]`;
        if (edge.tolerance != null) line += ` [tolerance: ${edge.tolerance}]`;
        if (edge.wait) line += ` [wait ${edge.wait}]`;
        if (edge.reflection) line += ` [reflection]`;
        sections.push(`- ${line}`);
      }
      sections.push("");
    }

    // Agents table
    const agents = ast.nodes.filter((n) => n.type === "agent") as AgentNode[];
    if (agents.length > 0) {
      sections.push("## Agents");
      sections.push("| Agent | Phase | Model | Role |");
      sections.push("|-------|-------|-------|------|");
      for (const agent of agents) {
        const phase = agent.phase != null ? String(agent.phase) : "-";
        const model = agent.model ?? "-";
        const role = agent.role ?? "-";
        sections.push(`| ${agent.id} | ${phase} | ${model} | ${role} |`);
      }
      sections.push("");
    }

    files.push({
      path: `.claude/commands/${trigger.name}.md`,
      content: sections.join("\n") + "\n",
      category: "machine",
    });
  }

  return files;
}

/** Generate workspace-protocol.md if workspace memory has a protocol field. */
function generateWorkspaceProtocol(ast: TopologyAST): GeneratedFile | null {
  const name = ast.topology.name;
  const workspace = ast.memory.workspace as Record<string, unknown> | undefined;
  if (!workspace || !workspace.protocol) return null;

  const protocol = workspace.protocol as string;
  const sections: string[] = [];
  sections.push("# Workspace Protocol");
  sections.push("");

  // Directory structure from workspace.structure
  const structure = workspace.structure;
  if (structure && Array.isArray(structure)) {
    sections.push("## Directory Structure");
    for (const item of structure) {
      sections.push(`- ${item}`);
    }
    sections.push("");
  }

  // Read/Write rules from agent nodes
  const agents = ast.nodes.filter((n) => n.type === "agent") as AgentNode[];
  if (agents.length > 0) {
    sections.push("## Read/Write Rules");
    for (const agent of agents) {
      const reads = agent.reads ?? [];
      const writes = agent.writes ?? [];
      if (reads.length > 0 || writes.length > 0) {
        sections.push(`### ${toTitle(agent.id)}`);
        if (reads.length > 0) {
          sections.push(`- Reads: ${reads.join(", ")}`);
        }
        if (writes.length > 0) {
          sections.push(`- Writes: ${writes.join(", ")}`);
        }
        sections.push("");
      }
    }
  }

  return {
    path: `.claude/skills/${name}/${protocol}`,
    content: sections.join("\n") + "\n",
    category: "machine",
  };
}

/**
 * Generate context/instructions file inside `.claude/`.
 *
 * If `ast.context.file` is set and points inside `.claude/`, that path is
 * used.  Otherwise the file is written to `.claude/CONTEXT.md`.
 *
 * **Important**: We never generate a file at the project-root `CLAUDE.md`
 * because that is the user's own instructions file and overwriting it would
 * destroy their content.
 */
function generateContextFile(ast: TopologyAST): GeneratedFile {
  const DEFAULT_PATH = ".claude/CONTEXT.md";
  let fileName = ast.context.file ?? DEFAULT_PATH;

  // Safety: never overwrite the user's root-level CLAUDE.md
  if (fileName === "CLAUDE.md" || fileName === "./CLAUDE.md") {
    fileName = DEFAULT_PATH;
  }
  const sections: string[] = [];

  sections.push(`# ${toTitle(ast.topology.name)}`);
  sections.push("");
  if (ast.topology.description) {
    sections.push(ast.topology.description);
    sections.push("");
  }
  sections.push(`Version: ${ast.topology.version}`);
  if (ast.topology.patterns.length > 0) {
    sections.push(`Patterns: ${ast.topology.patterns.join(", ")}`);
  }
  sections.push("");

  // Include references
  if (ast.context.includes && ast.context.includes.length > 0) {
    sections.push("## Includes");
    sections.push("");
    for (const inc of ast.context.includes) {
      sections.push(`- ${inc}`);
    }
    sections.push("");
  }

  // Defaults block
  if (ast.defaults) {
    const d = ast.defaults;
    sections.push("## Defaults");
    sections.push("");
    if (d.temperature != null) sections.push(`- Temperature: ${d.temperature}`);
    if (d.maxTokens != null) sections.push(`- Max tokens: ${d.maxTokens}`);
    if (d.topP != null) sections.push(`- Top-p: ${d.topP}`);
    if (d.topK != null) sections.push(`- Top-k: ${d.topK}`);
    if (d.stop) sections.push(`- Stop sequences: ${d.stop.join(", ")}`);
    if (d.seed != null) sections.push(`- Seed: ${d.seed}`);
    if (d.thinking) sections.push(`- Thinking: ${d.thinking}`);
    if (d.thinkingBudget != null) sections.push(`- Thinking budget: ${d.thinkingBudget}`);
    if (d.outputFormat) sections.push(`- Output format: ${d.outputFormat}`);
    if (d.timeout) sections.push(`- Timeout: ${d.timeout}`);
    if (d.logLevel) sections.push(`- Log level: ${d.logLevel}`);
    sections.push("");
  }

  // Providers
  if (ast.providers && ast.providers.length > 0) {
    sections.push("## Providers");
    sections.push("");
    for (const provider of ast.providers) {
      let line = `- **${provider.name}**`;
      if (provider.default) line += " (default)";
      if (provider.models.length > 0) line += ` — models: ${provider.models.join(", ")}`;
      sections.push(line);
      if (provider.baseUrl) sections.push(`  - Base URL: ${provider.baseUrl}`);
      if (provider.apiKey) sections.push(`  - API key: ${provider.apiKey}`);
      if (provider.auth) {
        sections.push(`  - Auth: ${provider.auth.type}`);
        if (provider.auth.issuer) sections.push(`    - Issuer: ${provider.auth.issuer}`);
        if (provider.auth.audience) sections.push(`    - Audience: ${provider.auth.audience}`);
        if (provider.auth.scopes) sections.push(`    - Scopes: ${provider.auth.scopes.join(", ")}`);
      }
    }
    sections.push("");
  }

  // Checkpoint config
  if (ast.checkpoint) {
    const cp = ast.checkpoint;
    sections.push("## Checkpoint");
    sections.push("");
    sections.push(`- Backend: ${cp.backend}`);
    sections.push(`- Strategy: ${cp.strategy}`);
    if (cp.connection) sections.push(`- Connection: ${cp.connection}`);
    if (cp.ttl) sections.push(`- TTL: ${cp.ttl}`);
    if (cp.replay) {
      sections.push(`- Replay: ${cp.replay.enabled ? "enabled" : "disabled"}`);
      if (cp.replay.maxHistory != null) sections.push(`  - Max history: ${cp.replay.maxHistory}`);
      if (cp.replay.branch != null) sections.push(`  - Branching: ${cp.replay.branch}`);
    }
    sections.push("");
  }

  // Artifacts registry
  if (ast.artifacts && ast.artifacts.length > 0) {
    sections.push("## Artifacts");
    sections.push("");
    for (const a of ast.artifacts) {
      let line = `- **${a.id}** (${a.type})`;
      if (a.path) line += ` — path: ${a.path}`;
      if (a.retention) line += `, retention: ${a.retention}`;
      if (a.dependsOn && a.dependsOn.length > 0) line += `, depends on: ${a.dependsOn.join(", ")}`;
      sections.push(line);
    }
    sections.push("");
  }

  return {
    path: fileName,
    content: sections.join("\n") + "\n",
    category: "machine",
  };
}

/**
 * Merge `ast.env` into the settings.json output under an `env` key.
 *
 * When `ast.providers` includes an "anthropic" provider with an api-key,
 * that env var is included automatically (enabling API-key mode instead
 * of subscription mode).
 *
 * Returns the env record if non-empty, otherwise null.
 */
function generateEnvToSettings(ast: TopologyAST): Record<string, string> | null {
  const env: Record<string, string> = {};
  for (const [key, val] of Object.entries(ast.env)) {
    if (typeof val === "string") {
      env[key] = val;
    } else {
      const sv = val as SensitiveValue;
      if (sv.secretRef) {
        // Secret reference — emit placeholder with comment-style URI
        env[key] = `\${${key}}`; // placeholder
        env[`# ${key}_SECRET`] = sv.secretRef.uri;
      } else if (sv.sensitive) {
        // Sensitive value — never emit the actual value
        env[key] = `\${${key}}`;
      } else if (sv.encrypted) {
        // Encrypted value — emit placeholder with warning
        env[key] = `\${${key}}`;
        env[`# ${key}_ENCRYPTED`] = "WARNING: value is SOPS-encrypted, decrypt before use";
      } else {
        env[key] = sv.value;
      }
    }
  }

  // If an anthropic provider exists, scaffold the API key env var
  if (ast.providers && ast.providers.length > 0) {
    const anthropicProvider = ast.providers.find((p) => p.name === "anthropic");
    if (anthropicProvider?.apiKey) {
      // Extract env var name from ${ENV_VAR} format
      const envVarMatch = anthropicProvider.apiKey.match(/^\$\{(.+)\}$/);
      if (envVarMatch) {
        env[envVarMatch[1]] = anthropicProvider.apiKey;
      }
    }
  }

  if (Object.keys(env).length === 0) return null;
  return env;
}

/** Generate trigger documentation in the skill. */
function generateTriggers(ast: TopologyAST): string {
  if (ast.triggers.length === 0) return "";

  const lines = ["## Triggers", ""];
  for (const trigger of ast.triggers) {
    lines.push(`### /${trigger.name}`);
    lines.push(`Pattern: \`${trigger.pattern}\``);
    if (trigger.argument) lines.push(`Argument: ${trigger.argument}`);
    lines.push("");
  }
  return lines.join("\n");
}

