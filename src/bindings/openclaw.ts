/** Convert a kebab-case id to Title Case. */
function toTitle(id: string): string {
  return id
    .split(/[-_]/)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(" ");
}

/** Generate a shell script stub with a header comment. */
function shellStub(description: string): string {
  return [
    "#!/usr/bin/env bash",
    `# ${description}`,
    "# Auto-generated by agentopology scaffold — edit as needed.",
    "set -euo pipefail",
    "",
    'echo "TODO: implement this script"',
    "",
  ].join("\n");
}

/** Generate an empty directory marker (a .gitkeep file). */
function gitkeep(dirPath: string): GeneratedFile {
  return { path: `${dirPath}/.gitkeep`, content: "", category: "machine" };
}

/**
 * Map topology model strings to OpenClaw model identifiers.
 * OpenClaw uses `provider/model-id` format with hyphens.
 */
function mapModel(model: string, defaultProvider?: string): string {
  const provider = defaultProvider ?? "anthropic";
  const MODEL_MAP: Record<string, string> = {
    opus: `${provider}/claude-opus-4-6`,
    sonnet: `${provider}/claude-sonnet-4-6`,
    haiku: `${provider}/claude-haiku-4-5`,
  };
  // If model already has provider prefix, return as-is
  if (model.includes("/")) return model;
  return MODEL_MAP[model] ?? `${provider}/${model}`;
}

/**
 * Map topology permission strings to OpenClaw permission labels.
 */
function mapPermissions(perm: string): string {
  switch (perm) {
    case "auto":
      return "autonomous";
    case "plan":
      return "supervised";
    case "confirm":
      return "interactive";
    case "bypass":
    case "bypassPermissions":
      return "unrestricted";
    case "supervised":
    case "autonomous":
    case "interactive":
    case "unrestricted":
      return perm;
    default:
      return "autonomous";
  }
}

/**
 * Extract openclaw-specific extension fields from the topology settings.
 */
function getOpenClawExtensions(
  ast: TopologyAST,
): Record<string, unknown> | null {
  const settingsExt = ast.settings?.extensions as
    | Record<string, Record<string, unknown>>
    | undefined;
  if (settingsExt?.openclaw) return settingsExt.openclaw;
  return null;
}

/** Get the orchestrator node from AST nodes. */
function getOrchestrator(ast: TopologyAST): OrchestratorNode | undefined {
  return ast.nodes.find((n) => n.type === "orchestrator") as
    | OrchestratorNode
    | undefined;
}

/** Get all agent nodes from AST, sorted by phase. */
function getAgentsSorted(ast: TopologyAST): AgentNode[] {
  const agents = ast.nodes.filter((n) => n.type === "agent") as AgentNode[];
  return agents.sort((a, b) => (a.phase ?? 0) - (b.phase ?? 0));
}

/** Get all gate nodes from AST. */
function getGates(ast: TopologyAST): GateNode[] {
  return ast.nodes.filter((n) => n.type === "gate") as GateNode[];
}

/** Get all human nodes from AST. */
function getHumanNodes(ast: TopologyAST): HumanNode[] {
  return ast.nodes.filter((n) => n.type === "human") as HumanNode[];
}

/** Get all group nodes from AST. */
function getGroupNodes(ast: TopologyAST): GroupNode[] {
  return ast.nodes.filter((n) => n.type === "group") as GroupNode[];
}

/** Format a schema field for documentation. */
function formatSchemaField(field: SchemaFieldDef): string {
  const opt = field.optional ? "?" : "";
  const typeStr = formatSchemaType(field.type);
  return `${field.name}${opt}: ${typeStr}`;
}

/** Format a schema type expression to a string. */
function formatSchemaType(t: SchemaFieldDef["type"]): string {
  switch (t.kind) {
    case "primitive":
      return t.value;
    case "array":
      return `${formatSchemaType(t.itemType)}[]`;
    case "enum":
      return t.values.join(" | ");
    case "ref":
      return t.name;
  }
}

/** Format retry config for documentation. */
function formatRetry(retry: number | RetryConfig): string {
  if (typeof retry === "number") return `max ${retry} attempts`;
  const parts = [`max ${retry.max} attempts`];
  if (retry.backoff) parts.push(`backoff: ${retry.backoff}`);
  if (retry.interval) parts.push(`interval: ${retry.interval}`);
  if (retry.maxInterval) parts.push(`max interval: ${retry.maxInterval}`);
  if (retry.jitter) parts.push("jitter: on");
  if (retry.nonRetryable && retry.nonRetryable.length > 0) {
    parts.push(`non-retryable: ${retry.nonRetryable.join(", ")}`);
  }
  return parts.join(", ");
}

