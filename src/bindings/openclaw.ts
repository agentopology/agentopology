/**
 * OpenClaw binding.
 *
 * Generates a complete OpenClaw workspace from a parsed {@link TopologyAST}:
 * openclaw.json, SOUL.md, AGENTS.md, TOOLS.md, MEMORY.md, BOOTSTRAP.md,
 * USER.md, IDENTITY.md, skill directories, gate/hook scripts, and workspace structure.
 *
 * OpenClaw is a multi-channel AI agent framework with gateway, channels,
 * and markdown-based agent identity. This binding adds security hardening
 * (auth required by default), quality gates, bounded loops, structured
 * metrics, and workspace access control that OpenClaw lacks natively.
 *
 * @module
 */

import type {
  TopologyAST,
  AgentNode,
  GateNode,
  GroupNode,
  HumanNode,
  OrchestratorNode,
  SchemaFieldDef,
  RetryConfig,
} from "../parser/ast.js";
import { deduplicateFiles } from "./types.js";
import type { BindingTarget, GeneratedFile } from "./types.js";

