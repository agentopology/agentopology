/** Generate BOOTSTRAP.md — init instructions from hooks + gates. */
function generateBootstrapMd(ast: TopologyAST): GeneratedFile {
  const agents = getAgentsSorted(ast);
  const gates = getGates(ast);
  const sections: string[] = [];

  sections.push("# Bootstrap Sequence");
  sections.push("");

  // Initialization hooks
  const initHooks = ast.hooks.filter(
    (h) => h.on === "SessionStart" || h.on === "InstructionsLoaded",
  );
  if (initHooks.length > 0) {
    sections.push("## Initialization");
    let idx = 1;
    for (const hook of initHooks) {
      sections.push(`${idx}. Run: ${hook.run}`);
      idx++;
    }
    sections.push("");
  }

  // Pre-flight gates (gates before the first flow node)
  const firstEdge = ast.edges.length > 0 ? ast.edges[0] : null;
  const preFlightGates = gates.filter(
    (g) => firstEdge && g.before === firstEdge.from,
  );
  if (preFlightGates.length > 0) {
    sections.push("## Pre-Flight Gates");
    sections.push("");
    sections.push("> **Enforcement note:** Gate scripts are advisory — they must be run manually");
    sections.push("> between agents. Tool deny lists (`tools.deny` in openclaw.json) ARE enforced");
    sections.push("> by the runtime. Gate scripts provide quality checks but not hard enforcement.");
    sections.push("");
    let idx = 1;
    for (const gate of preFlightGates) {
      sections.push(`${idx}. Gate: ${gate.id} -- ${gate.run ?? "manual check"}`);
      sections.push(`   On failure: ${gate.onFail ?? "halt"}`);
      idx++;
    }
    sections.push("");
  }

  // Agent initialization order
  if (agents.length > 0) {
    sections.push("## Agent Initialization Order");
    sections.push("");
    for (const agent of agents) {
      const phase = agent.phase != null ? agent.phase : 0;
      sections.push(
        `Phase ${phase}: ${agent.id} (${mapModel(agent.model ?? "sonnet")})`,
      );
    }
    sections.push("");
  }

  return {
    path: "BOOTSTRAP.md",
    content: sections.join("\n") + "\n",
    category: "machine",
  };
}

