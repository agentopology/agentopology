/**
 * Google Gemini CLI binding.
 *
 * Generates the `.gemini/` directory structure, `GEMINI.md`, and supporting
 * files from a parsed {@link TopologyAST}.
 *
 * Gemini CLI structure:
 *   GEMINI.md                     — Main instructions (like CLAUDE.md)
 *   .gemini/settings.json         — Tool permissions, model config, sandbox
 *   .gemini/instructions.md       — Detailed project instructions
 *   .gemini/commands/<name>.md    — Custom command files from triggers
 *
 * @module
 */

import type {
  TopologyAST,
  AgentNode,
  GateNode,
  HookDef,
  SkillDef,
  ToolBlockDef,
} from "../parser/ast.js";
import type { BindingTarget, GeneratedFile } from "./types.js";

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

/** Convert a kebab-case id to Title Case. */
function toTitle(id: string): string {
  return id
    .split(/[-_]/)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(" ");
}

/** Generate a shell script stub with a header comment. */
function shellStub(description: string): string {
  return [
    "#!/usr/bin/env bash",
    `# ${description}`,
    "# Auto-generated by agentopology scaffold — edit as needed.",
    'set -euo pipefail',
    "",
    'echo "TODO: implement this script"',
    "",
  ].join("\n");
}

/** Generate an empty directory marker (a .gitkeep file). */
function gitkeep(dirPath: string): GeneratedFile {
  return { path: `${dirPath}/.gitkeep`, content: "" };
}

/**
 * Map topology permission strings to Gemini CLI approval settings.
 *
 * Gemini CLI permission model:
 *   supervised  -> default (asks for approval)
 *   autonomous  -> yolo (auto-approve all tools)
 *   interactive -> default (asks for approval)
 *   unrestricted -> sandbox off + yolo
 *
 * Returns a partial settings object to merge.
 */
function mapPermissions(perm: string): { sandbox?: boolean; toolApproval?: string } {
  switch (perm) {
    case "supervised":
      return { toolApproval: "default" };
    case "autonomous":
      return { toolApproval: "yolo" };
    case "interactive":
      return { toolApproval: "default" };
    case "unrestricted":
      return { sandbox: false, toolApproval: "yolo" };
    default:
      return {};
  }
}

