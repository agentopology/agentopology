/** Convert a kebab-case id to Title Case. */
function toTitle(id: string): string {
  return id
    .split(/[-_]/)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(" ");
}

/**
 * Map topology permission strings to Codex approval_policy values.
 *
 * Codex CLI supports three approval policies:
 *   - "suggest"    — shows diffs for approval (default, most restrictive)
 *   - "auto-edit"  — auto-applies file changes, asks for commands
 *   - "full-auto"  — auto-runs everything
 */
function mapApprovalPolicy(perm: string): string {
  // Codex-native values — pass through
  const nativeValues = ["suggest", "auto-edit", "full-auto"];
  if (nativeValues.includes(perm)) return perm;

  // Topology-level semantic values
  switch (perm) {
    case "supervised":
      return "suggest";
    case "autonomous":
      return "auto-edit";
    case "interactive":
      return "suggest";
    case "unrestricted":
      return "full-auto";
    // Claude Code native values mapped to Codex equivalents
    case "plan":
      return "suggest";
    case "auto":
      return "auto-edit";
    case "confirm":
      return "suggest";
    case "bypassPermissions":
      return "full-auto";
    default:
      return "suggest"; // safe default
  }
}

/**
 * Escape a TOML string value.
 * Wraps in double quotes, escaping internal quotes and backslashes.
 */
function tomlString(value: string): string {
  const escaped = value
    .replace(/\\/g, "\\\\")
    .replace(/"/g, '\\"')
    .replace(/\n/g, "\\n");
  return `"${escaped}"`;
}

/** Format a TOML array of strings on a single line. */
function tomlStringArray(values: string[]): string {
  return `[${values.map(tomlString).join(", ")}]`;
}

/** Generate a shell script stub with a header comment. */
function shellStub(description: string): string {
  return [
    "#!/usr/bin/env bash",
    `# ${description}`,
    "# Auto-generated by agentopology scaffold — edit as needed.",
    "set -euo pipefail",
    "",
    'echo "TODO: implement this script"',
    "",
  ].join("\n");
}

/** Generate an empty directory marker (a .gitkeep file). */
function gitkeep(dirPath: string): GeneratedFile {
  return { path: `${dirPath}/.gitkeep`, content: "" };
}

