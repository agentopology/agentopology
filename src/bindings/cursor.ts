/** Convert a kebab-case id to Title Case. */
function toTitle(id: string): string {
  return id
    .split(/[-_]/)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(" ");
}

/**
 * Downshift all markdown headings by one level so they nest properly
 * inside ## Instructions. ## -> ###, ### -> ####, etc.
 */
function downshiftHeadings(text: string): string {
  return text.replace(/^(#{2,6})\s/gm, (_, hashes: string) => `#${hashes} `);
}

/**
 * Generate .mdc-style YAML frontmatter.
 *
 * Only three fields are valid for Cursor: description, globs, alwaysApply.
 * Fields with undefined/null values are omitted. When alwaysApply is false
 * (the default), it is omitted.
 */
function frontmatter(fields: {
  description?: string;
  globs?: string[];
  alwaysApply?: boolean;
}): string {
  const lines = ["---"];
  // Always emit all three fields (matching real-world .mdc convention).
  // description and globs are empty when not set; alwaysApply defaults to false.
  lines.push(`description: ${fields.description ?? ""}`);
  if (fields.globs && fields.globs.length > 0) {
    // Real .mdc files use comma-separated globs on a single line
    lines.push(`globs: ${fields.globs.join(", ")}`);
  } else {
    lines.push("globs:");
  }
  lines.push(`alwaysApply: ${fields.alwaysApply ? "true" : "false"}`);
  lines.push("---");
  return lines.join("\n");
}

/** Generate a shell script stub with a header comment. */
function shellStub(description: string): string {
  return [
    "#!/usr/bin/env bash",
    `# ${description}`,
    "# Auto-generated by agentopology scaffold -- edit as needed.",
    "set -euo pipefail",
    "",
    'echo "TODO: implement this script"',
    "",
  ].join("\n");
}

/** Generate an empty directory marker (a .gitkeep file). */
function gitkeep(dirPath: string): GeneratedFile {
  return { path: `${dirPath}/.gitkeep`, content: "", category: "machine" };
}

/**
 * Convert PascalCase .at hook event to camelCase Cursor event name.
 * e.g. PreToolUse -> preToolUse, SessionStart -> sessionStart
 */
function mapHookEvent(atEvent: string): string {
  return atEvent.charAt(0).toLowerCase() + atEvent.slice(1);
}

/** Format a SchemaType to a human-readable string. */
function formatSchemaType(t: SchemaType, schemas?: SchemaDef[]): string {
  switch (t.kind) {
    case "primitive":
      return t.value;
    case "array":
      return `${formatSchemaType(t.itemType, schemas)}[]`;
    case "enum":
      return t.values.join(" | ");
    case "ref": {
      if (schemas) {
        const resolved = schemas.find((s) => s.id === t.name);
        if (resolved) {
          const fields = resolved.fields.map(
            (f) =>
              `${f.name}${f.optional ? "?" : ""}: ${formatSchemaType(f.type, schemas)}`,
          );
          return `${t.name} { ${fields.join(", ")} }`;
        }
      }
      return t.name;
    }
  }
}

/** Format a SchemaFieldDef to a readable line. */
function formatSchemaField(f: SchemaFieldDef): string {
  const opt = f.optional ? "?" : "";
  return `- ${f.name}${opt}: ${formatSchemaType(f.type)}`;
}

/**
 * Compile edges into natural-language workflow instructions.
 */
function compileEdges(ast: TopologyAST): string {
  if (ast.edges.length === 0) return "";

  const lines: string[] = ["## Workflow", ""];
  const nodeMap = new Map(ast.nodes.map((n) => [n.id, n.label]));
  const label = (id: string): string => nodeMap.get(id) ?? toTitle(id);

  let step = 1;
  for (const edge of ast.edges) {
    const parts: string[] = [];

    if (edge.isError) {
      const errType = edge.errorType ? `${edge.errorType} ` : "";
      parts.push(
        `${step}. On ${errType}error from **${label(edge.from)}**, route to **${label(edge.to)}**`,
      );
    } else if (edge.condition) {
      parts.push(
        `${step}. If ${edge.condition}, proceed to **${label(edge.to)}**`,
      );
    } else if (edge.reflection) {
      parts.push(
        `${step}. Loop back to **${label(edge.from)}** for self-evaluation`,
      );
    } else {
      parts.push(
        `${step}. After **${label(edge.from)}**, proceed to **${label(edge.to)}**`,
      );
    }

    if (edge.maxIterations) {
      parts.push(`   - Repeat up to ${edge.maxIterations} times`);
    }
    if (edge.per) {
      parts.push(
        `   - For each ${edge.per} item, run **${label(edge.to)}**`,
      );
    }
    if (edge.race) {
      parts.push(
        `   - Race **${label(edge.to)}** agents -- use first result`,
      );
    }
    if (edge.tolerance != null) {
      parts.push(`   - Allow up to ${edge.tolerance} failures`);
    }
    if (edge.wait) {
      parts.push(
        `   - Wait ${edge.wait} before proceeding to **${label(edge.to)}**`,
      );
    }
    if (edge.weight != null) {
      parts.push(
        `   - Route to **${label(edge.to)}** with weight ${edge.weight}`,
      );
    }

    lines.push(...parts);
    step++;
  }

  // Delegation summary
  const agents = ast.nodes.filter((n) => n.type === "agent") as AgentNode[];
  if (agents.length > 0) {
    lines.push("");
    lines.push("### Delegation");
    for (const agent of agents) {
      const desc = agent.description ?? agent.label;
      lines.push(`- **${agent.id}**: ${desc} -- activate with \`@${agent.id}\``);
    }
  }

  lines.push("");
  return lines.join("\n");
}

