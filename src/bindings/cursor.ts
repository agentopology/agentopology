/** Generate .cursor/mcp.json from global and per-agent MCP servers. */
function generateMcpJson(ast: TopologyAST): GeneratedFile | null {
  // Collect all MCP servers (global + per-agent)
  const allServers: Record<string, Record<string, unknown>> = {};

  // Global MCP servers
  for (const [name, config] of Object.entries(ast.mcpServers)) {
    allServers[name] = config;
  }

  // Per-agent MCP servers: merge by reference
  for (const node of ast.nodes) {
    if (node.type !== "agent") continue;
    const agent = node as AgentNode;
    if (!agent.mcpServers || agent.mcpServers.length === 0) continue;
    // Agent mcpServers are just names (strings), they reference global config
    // The actual config comes from ast.mcpServers
  }

  if (Object.keys(allServers).length === 0) return null;

  const mcpConfig: Record<string, unknown> = { mcpServers: {} };
  const servers = mcpConfig.mcpServers as Record<string, unknown>;

  for (const [name, config] of Object.entries(allServers)) {
    const entry: Record<string, unknown> = {};
    for (const [key, value] of Object.entries(config)) {
      if (key === "env" && typeof value === "object" && value !== null) {
        // Handle SensitiveValue env vars
        const envObj: Record<string, string> = {};
        for (const [envKey, envVal] of Object.entries(
          value as Record<string, unknown>,
        )) {
          if (typeof envVal === "string") {
            envObj[envKey] = envVal;
          } else if (
            typeof envVal === "object" &&
            envVal !== null &&
            "value" in envVal
          ) {
            const sv = envVal as SensitiveValue;
            if (sv.sensitive) {
              envObj[envKey] = `\${${envKey}}`;
            } else {
              envObj[envKey] = sv.value;
            }
          }
        }
        entry.env = envObj;
      } else if (key === "args" && Array.isArray(value)) {
        entry.args = value;
      } else if (key === "command" || key === "url") {
        // Valid MCP server fields: command (stdio), url (SSE)
        entry[key] = value;
      }
      // Skip unknown fields — the parser may leak env vars to top level
    }
    // Only include env when there are actual env vars (real Cursor configs omit it)
    servers[name] = entry;
  }

  return {
    path: ".cursor/mcp.json",
    content: JSON.stringify(mcpConfig, null, 2) + "\n",
    category: "shared-config",
  };
}

/**
 * Generate .cursor/hooks.json from global hooks, per-agent hooks, gate nodes,
 * and metering configuration.
 */
function generateHooksJson(ast: TopologyAST): GeneratedFile | null {
  const hooksByEvent: Record<string, unknown[]> = {};

  // Helper to add a hook entry
  const addHook = (
    event: string,
    entry: Record<string, unknown>,
  ): void => {
    if (!hooksByEvent[event]) hooksByEvent[event] = [];
    hooksByEvent[event].push(entry);
  };

  // Global hooks
  for (const hook of ast.hooks) {
    const event = mapHookEvent(hook.on);
    const scriptName = hook.run.replace(/^.*\//, "").replace(/\s.*$/, "");
    const command = hook.run.startsWith("./")
      ? hook.run
      : `.cursor/hooks/${scriptName}`;

    const cursorExt = hook.extensions?.cursor as
      | Record<string, unknown>
      | undefined;

    const entry: Record<string, unknown> = {
      command,
    };
    if (hook.matcher) entry.matcher = hook.matcher;
    if (hook.timeout != null) entry.timeout = Math.ceil(hook.timeout / 1000);
    // Cursor hooks do NOT have a `type` field (unlike Claude Code).
    // Only include failClosed when true (false is the default).
    const failClosed = (cursorExt?.failClosed as boolean | undefined) ?? false;
    if (failClosed) entry.failClosed = true;
    if (cursorExt?.loop_limit != null) {
      entry.loop_limit = cursorExt.loop_limit;
    }

    addHook(event, entry);
  }

  // Per-agent hooks
  for (const node of ast.nodes) {
    if (node.type !== "agent") continue;
    const agent = node as AgentNode;
    if (!agent.hooks || agent.hooks.length === 0) continue;

    for (const hook of agent.hooks) {
      const event = mapHookEvent(hook.on);
      const scriptName = hook.run.replace(/^.*\//, "").replace(/\s.*$/, "");
      const command = hook.run.startsWith("./")
        ? hook.run
        : `.cursor/hooks/${agent.id}-${scriptName}`;

      const cursorExt = hook.extensions?.cursor as
        | Record<string, unknown>
        | undefined;

      const entry: Record<string, unknown> = {
        command,
      };
      if (hook.matcher) entry.matcher = hook.matcher;
      else entry.matcher = agent.id;
      if (hook.timeout != null) entry.timeout = Math.ceil(hook.timeout / 1000);
      // Cursor hooks do NOT have a `type` field.
      const agentFailClosed = (cursorExt?.failClosed as boolean | undefined) ?? false;
      if (agentFailClosed) entry.failClosed = true;
      if (cursorExt?.loop_limit != null) {
        entry.loop_limit = cursorExt.loop_limit;
      }

      addHook(event, entry);
    }
  }

  // Gate compilation to hooks
  const gates = ast.nodes.filter((n) => n.type === "gate") as GateNode[];
  for (const gate of gates) {
    const scriptPath = `.cursor/hooks/gate-${gate.id}.sh`;

    // Determine event
    let event: string;
    if (gate.before) {
      event = "preToolUse";
    } else if (gate.after) {
      event = "postToolUse";
    } else {
      event = "preToolUse";
    }

    // Determine matcher
    const matcher = gate.after ?? gate.before ?? undefined;

    // Determine failClosed
    const failClosed =
      gate.behavior === "blocking" || gate.onFail === "halt";

    const entry: Record<string, unknown> = {
      command: scriptPath,
    };
    if (failClosed) entry.failClosed = true;
    if (matcher) entry.matcher = matcher;
    if (gate.timeout) {
      // Parse duration string to seconds (best-effort)
      const match = gate.timeout.match(/^(\d+)(s|m|h)?$/);
      if (match) {
        const val = parseInt(match[1], 10);
        const unit = match[2] ?? "s";
        const seconds =
          unit === "h" ? val * 3600 : unit === "m" ? val * 60 : val;
        entry.timeout = seconds;
      }
    }

    const cursorExt = gate.extensions?.cursor as
      | Record<string, unknown>
      | undefined;
    if (cursorExt?.loop_limit != null) {
      entry.loop_limit = cursorExt.loop_limit;
    }

    addHook(event, entry);
  }

  // Metering -> postToolUse hook
  if (ast.metering) {
    addHook("postToolUse", {
      command: ".cursor/hooks/metering.sh",
    });
  }

  if (Object.keys(hooksByEvent).length === 0) return null;

  const hooksJson = {
    version: 1,
    hooks: hooksByEvent,
  };

  return {
    path: ".cursor/hooks.json",
    content: JSON.stringify(hooksJson, null, 2) + "\n",
    category: "shared-config",
  };
}

