/**
 * Generate gate hooks and scripts.
 *
 * Returns gate scripts as GeneratedFile[]. The hook entries are collected
 * by generateHooksJson separately.
 */
function generateGateHooks(ast: TopologyAST): GeneratedFile[] {
  const files: GeneratedFile[] = [];

  for (const node of ast.nodes) {
    if (node.type !== "gate") continue;
    const gate = node as GateNode;

    const lines: string[] = [
      "#!/usr/bin/env bash",
      `# Gate: ${gate.id}`,
      "# Auto-generated by agentopology scaffold -- edit as needed.",
      "set -euo pipefail",
      "",
    ];

    lines.push(`# After: ${gate.after ?? "any"}`);
    lines.push(`# Before: ${gate.before ?? "any"}`);
    if (gate.onFail) lines.push(`# On failure: ${gate.onFail}`);
    if (gate.behavior) lines.push(`# Behavior: ${gate.behavior}`);
    if (gate.timeout) lines.push(`# Timeout: ${gate.timeout}`);
    lines.push("");

    if (gate.run) {
      lines.push(`# Run: ${gate.run}`);
      lines.push("");

      if (gate.retry != null && gate.retry > 0) {
        lines.push(`MAX_RETRIES=${gate.retry}`);
        lines.push("ATTEMPT=0");
        lines.push("GATE_RESULT=1");
        lines.push("");
        lines.push(
          "while [ $ATTEMPT -le $MAX_RETRIES ] && [ $GATE_RESULT -ne 0 ]; do",
        );
        lines.push("  ATTEMPT=$((ATTEMPT + 1))");
        lines.push(
          `  ${gate.run} && GATE_RESULT=0 || GATE_RESULT=$?`,
        );
        lines.push(
          '  if [ $GATE_RESULT -ne 0 ] && [ $ATTEMPT -le $MAX_RETRIES ]; then',
        );
        lines.push(
          `    echo "Gate '${gate.id}' attempt $ATTEMPT failed, retrying..."`,
        );
        lines.push("    sleep 1");
        lines.push("  fi");
        lines.push("done");
      } else {
        lines.push("GATE_RESULT=0");
        lines.push(
          `${gate.run} || GATE_RESULT=$?`,
        );
      }
    } else if (gate.checks && gate.checks.length > 0) {
      lines.push("# Checks:");
      lines.push("GATE_RESULT=0");
      lines.push("");
      for (const check of gate.checks) {
        lines.push(`# Check: ${check}`);
        lines.push(`echo "TODO: implement check '${check}'"  # || GATE_RESULT=1`);
      }
    } else {
      lines.push("GATE_RESULT=0");
      lines.push('echo "TODO: implement gate check"');
    }

    lines.push("");
    const exitAction =
      gate.onFail === "halt" || gate.behavior === "blocking"
        ? "exit 1"
        : 'echo "Gate failed -- advisory only"';
    lines.push('if [ "$GATE_RESULT" -ne 0 ]; then');
    lines.push(`  echo "Gate '${gate.id}' FAILED (exit code $GATE_RESULT)"`);
    lines.push(`  ${exitAction}`);
    lines.push("fi");
    lines.push("");

    files.push({
      path: `.cursor/hooks/gate-${gate.id}.sh`,
      content: lines.join("\n"),
      category: "script",
    });
  }

  return files;
}

