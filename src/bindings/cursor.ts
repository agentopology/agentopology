/**
 * Cursor IDE binding.
 *
 * Generates `.cursor/rules/*.mdc`, `.cursor/mcp.json`, `.cursor/hooks.json`,
 * `AGENTS.md`, and supporting shell scripts from a parsed {@link TopologyAST}.
 *
 * Cursor rules use `.mdc` files with YAML frontmatter supporting only three
 * fields: `description`, `globs`, and `alwaysApply`. All other AST concepts
 * are either mapped to hooks.json, shell scripts, or embedded as polyfill
 * instructions in rule body text.
 *
 * @module
 */

