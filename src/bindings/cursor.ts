/** Cursor IDE binding. */
export const cursorBinding: BindingTarget = {
  name: "cursor",
  description:
    "Cursor IDE -- generates .cursor/rules/*.mdc, .cursor/mcp.json, .cursor/hooks.json, and scripts.",

  scaffold(ast: TopologyAST): GeneratedFile[] {
    // Fragment check
    if (ast.isFragment) {
      return deduplicateFiles([
        generateTopologyOverview(ast),
        ...generateAgentRules(ast),
      ]);
    }

    const files: GeneratedFile[] = [];

    // Rules
    files.push(generateTopologyOverview(ast));
    files.push(...generateAgentRules(ast));
    files.push(...generateHumanRules(ast));
    files.push(...generateGroupRules(ast));
    files.push(...generateSkillRules(ast));
    files.push(...generateTriggerRules(ast));
    files.push(...generateSchemas(ast));

    // Scripts
    files.push(...generateActionScripts(ast));
    files.push(...generateGateHooks(ast));
    files.push(...generateHookScripts(ast));
    files.push(...generateAgentHookScripts(ast));
    files.push(...generateToolScripts(ast));
    files.push(...generateScheduleScripts(ast));
    files.push(...generateObservabilityScripts(ast));

    // Config files
    const mcpFile = generateMcpJson(ast);
    if (mcpFile) files.push(mcpFile);

    const hooksFile = generateHooksJson(ast);
    if (hooksFile) files.push(hooksFile);

    const meteringFile = generateMeteringScript(ast);
    if (meteringFile) files.push(meteringFile);

    // Context
    files.push(generateContextFile(ast));

    // Legacy
    const legacyFile = generateCursorrules(ast);
    if (legacyFile) files.push(legacyFile);

    // Memory
    files.push(...generateMemory(ast));

    return deduplicateFiles(files);
  },
};
