function generateContextFile(ast: TopologyAST): GeneratedFile {
  const DEFAULT_PATH = "AGENTS.md";
  const fileName = ast.context.file ?? DEFAULT_PATH;

  const sections: string[] = [];

  sections.push(`# ${ast.topology.name}`);
  sections.push("");
  if (ast.topology.description) {
    sections.push(ast.topology.description);
    sections.push("");
  }

  // Agent roster table
  const allAgents = ast.nodes.filter(
    (n) =>
      n.type === "agent" || n.type === "human" || n.type === "group",
  );
  if (allAgents.length > 0) {
    sections.push("## Agents");
    sections.push("");
    sections.push("| Agent | Description |");
    sections.push("|-------|-------------|");
    for (const node of allAgents) {
      let desc = node.label;
      if (node.type === "agent") {
        desc = (node as AgentNode).description ?? node.label;
      } else if (node.type === "human") {
        desc = (node as HumanNode).description ?? node.label;
      } else if (node.type === "group") {
        desc = (node as GroupNode).description ?? node.label;
      }
      sections.push(`| ${node.id} | ${desc} |`);
    }
    sections.push("");
  }

  // Workflow summary (compiled edges)
  const edgeText = compileEdges(ast);
  if (edgeText) {
    sections.push(edgeText);
  }

  // Rules usage guide
  sections.push("## Rules");
  sections.push("");
  sections.push(
    "- Use `@{rule-name}` to activate specific agent rules",
  );
  sections.push("- Agent rules are in `.cursor/rules/`");
  sections.push("- MCP servers configured in `.cursor/mcp.json`");
  sections.push(
    "- Cursor supports Enterprise > Team > Project > User rule hierarchy. These are Project-level rules.",
  );
  sections.push("");

  // Providers
  if (ast.providers && ast.providers.length > 0) {
    sections.push("## Providers");
    sections.push("");
    for (const provider of ast.providers) {
      let line = `- **${provider.name}**`;
      if (provider.default) line += " (default)";
      if (provider.models.length > 0)
        line += ` -- models: ${provider.models.join(", ")}`;
      sections.push(line);
    }
    sections.push("");
  }

  // Checkpoint
  if (ast.checkpoint) {
    const cp = ast.checkpoint;
    sections.push("## Checkpoint");
    sections.push("");
    sections.push(`- Backend: ${cp.backend}`);
    sections.push(`- Strategy: ${cp.strategy}`);
    if (cp.connection) sections.push(`- Connection: ${cp.connection}`);
    if (cp.ttl) sections.push(`- TTL: ${cp.ttl}`);
    sections.push("");
  }

  // Artifacts
  if (ast.artifacts && ast.artifacts.length > 0) {
    sections.push("## Artifacts");
    sections.push("");
    for (const a of ast.artifacts) {
      let line = `- **${a.id}** (${a.type})`;
      if (a.path) line += ` -- path: ${a.path}`;
      if (a.retention) line += `, retention: ${a.retention}`;
      sections.push(line);
    }
    sections.push("");
  }

  // Context includes
  if (ast.context.includes && ast.context.includes.length > 0) {
    sections.push("## Includes");
    sections.push("");
    for (const inc of ast.context.includes) {
      sections.push(`- ${inc}`);
    }
    sections.push("");
  }

  return {
    path: fileName,
    content: sections.join("\n"),
    category: "agent",
  };
}

/** Generate legacy .cursorrules file (flat markdown, no frontmatter). */
function generateCursorrules(ast: TopologyAST): GeneratedFile | null {
  // Only generate if explicitly requested via extensions
  const cursorExt = ast.extensions?.cursor as
    | Record<string, unknown>
    | undefined;
  if (!cursorExt || cursorExt.legacyRules !== true) return null;

  const sections: string[] = [];
  sections.push(`# ${ast.topology.name}`);
  sections.push("");
  if (ast.topology.description) {
    sections.push(ast.topology.description);
    sections.push("");
  }

  // Agent summaries
  const agents = ast.nodes.filter(
    (n) => n.type === "agent" || n.type === "human" || n.type === "group",
  );
  if (agents.length > 0) {
    sections.push("## Agents");
    sections.push("");
    for (const node of agents) {
      let desc = node.label;
      if (node.type === "agent") {
        desc = (node as AgentNode).description ?? node.label;
      } else if (node.type === "human") {
        desc = (node as HumanNode).description ?? node.label;
      } else if (node.type === "group") {
        desc = (node as GroupNode).description ?? node.label;
      }
      sections.push(`- **${node.id}**: ${desc}`);
    }
    sections.push("");
  }

  // Workflow
  const edgeText = compileEdges(ast);
  if (edgeText) {
    sections.push(edgeText);
  }

  // Settings/defaults as behavioral text
  if (ast.defaults) {
    const d = ast.defaults;
    sections.push("## Defaults");
    sections.push("");
    if (d.temperature != null) sections.push(`- Temperature: ${d.temperature}`);
    if (d.maxTokens != null) sections.push(`- Max tokens: ${d.maxTokens}`);
    if (d.timeout) sections.push(`- Timeout: ${d.timeout}`);
    sections.push("");
  }

  return {
    path: ".cursorrules",
    content: sections.join("\n"),
    category: "agent",
  };
}

/** Generate memory directory markers. */
function generateMemory(ast: TopologyAST): GeneratedFile[] {
  const files: GeneratedFile[] = [];
  const memKeys = Object.keys(ast.memory);
  if (memKeys.length > 0) {
    files.push(gitkeep(".cursor/memory"));
  }
  return files;
}

/** Generate observability hook scripts. */
function generateObservabilityScripts(ast: TopologyAST): GeneratedFile[] {
  const files: GeneratedFile[] = [];
  if (!ast.observability || !ast.observability.enabled) return files;

  const o = ast.observability;
  const lines: string[] = [
    "#!/usr/bin/env bash",
    "# Observability capture script",
    "# Auto-generated by agentopology scaffold -- edit as needed.",
    "set -euo pipefail",
    "",
    `# Exporter: ${o.exporter}`,
    `# Level: ${o.level}`,
    `# Sample rate: ${o.sampleRate}`,
  ];
  if (o.endpoint) lines.push(`# Endpoint: ${o.endpoint}`);
  if (o.service) lines.push(`# Service: ${o.service}`);
  lines.push("");
  lines.push("# Read hook input from stdin");
  lines.push("INPUT=$(cat)");
  lines.push("");
  lines.push('echo "TODO: forward trace data to exporter"');
  lines.push("");

  files.push({
    path: ".cursor/scripts/observability.sh",
    content: lines.join("\n"),
    category: "script",
  });

  return files;
}

/** Generate schedule scripts. */
function generateScheduleScripts(ast: TopologyAST): GeneratedFile[] {
  const files: GeneratedFile[] = [];
  if (!ast.schedules || ast.schedules.length === 0) return files;

  for (const job of ast.schedules) {
    const target = job.agent
      ? `agent ${job.agent}`
      : job.action
        ? `action ${job.action}`
        : "unknown";

    const lines: string[] = [
      "#!/usr/bin/env bash",
      `# Schedule: ${job.id} -- invokes ${target}`,
      "# Auto-generated by agentopology scaffold -- edit as needed.",
      "set -euo pipefail",
      "",
    ];

    if (job.enabled === false) {
      lines.push("# DISABLED -- this schedule is not enabled");
      lines.push("exit 0");
      lines.push("");
    }

    if (job.cron) {
      lines.push(`# Cron: ${job.cron}`);
    } else if (job.every) {
      lines.push(`# Every: ${job.every}`);
    }

    if (job.agent) {
      lines.push(`# Invoke agent: ${job.agent}`);
      lines.push(`echo "TODO: invoke agent ${job.agent}"`);
    } else if (job.action) {
      lines.push(`# Invoke action: ${job.action}`);
      lines.push(`bash ./.cursor/scripts/actions/${job.action}.sh`);
    }
    lines.push("");

    files.push({
      path: `.cursor/scripts/schedule-${job.id}.sh`,
      content: lines.join("\n"),
      category: "script",
    });
  }

  return files;
}

/** Generate schema documentation files. */
function generateSchemas(ast: TopologyAST): GeneratedFile[] {
  const files: GeneratedFile[] = [];
  if (!ast.schemas || ast.schemas.length === 0) return files;

  for (const schema of ast.schemas) {
    const sections: string[] = [];
    sections.push(`# Schema: ${schema.id}`);
    sections.push("");
    sections.push("## Fields");
    sections.push("");
    for (const field of schema.fields) {
      const opt = field.optional ? " (optional)" : "";
      sections.push(
        `- **${field.name}**${opt}: ${formatSchemaType(field.type, ast.schemas)}`,
      );
    }
    sections.push("");

    // Embed as a rule that agents can reference
    const fm = frontmatter({
      description: `Schema definition: ${schema.id}`,
    });

    files.push({
      path: `.cursor/rules/schema-${schema.id}.mdc`,
      content: fm + "\n\n" + sections.join("\n"),
      category: "agent",
    });
  }

  return files;
}

